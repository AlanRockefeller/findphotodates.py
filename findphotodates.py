def test_date_parsing():
    """Test function to verify date parsing and aggregation works correctly."""
    from collections import Counter, defaultdict
    
    # Test data with exiftool date format - using 7-tuples matching main code structure
    # (filepath, date_taken, size_bytes, mtime_ns, gps_lat, gps_lon, location)
    test_photo_data = [
        ("/path/to/file1.jpg", "2025:12:31 23:59:59", 1000, 1234567890, "37.7749", "-122.4194", None),
        ("/path/to/file2.jpg", "2025:12:31 12:00:00", 2000, 1234567891, "34.0522", "-118.2437", None),
        ("/path/to/file3.jpg", "2025:12:30 10:30:00", 3000, 1234567892, None, None, None),
        ("/path/to/file4.jpg", "2025:11:15 08:15:00", 4000, 1234567893, "40.7128", "-74.0060", None),
        ("/path/to/file5.jpg", "2025:11:15 20:45:00", 5000, 1234567894, None, None, None),
        ("/path/to/file6.jpg", None, 6000, 1234567895, None, None, None),  # No date
        ("/path/to/file7.jpg", "invalid-date", 7000, 1234567896, None, None, None),  # Invalid date
    ]
    
    file_counts = Counter({"jpg": 7})
    
    # Test date parsing
    date_counter = Counter()
    for entry in test_photo_data:
        # Entry is a tuple: (filepath, date_taken, size_bytes, mtime_ns, gps_lat, gps_lon, location)
        date_str = entry[1] if len(entry) > 1 else None
        if not date_str:
            continue
        try:
            dt = datetime.strptime(date_str, "%Y:%m:%d %H:%M:%S")
            day_key = dt.strftime("%Y-%m-%d")
            date_counter[day_key] += 1
        except (ValueError, AttributeError, TypeError):
            continue
    
    # Verify day aggregation
    assert date_counter["2025-12-31"] == 2, f"Expected 2 files on 2025-12-31, got {date_counter.get('2025-12-31', 0)}"
    assert date_counter["2025-12-30"] == 1, f"Expected 1 file on 2025-12-30, got {date_counter.get('2025-12-30', 0)}"
    assert date_counter["2025-11-15"] == 2, f"Expected 2 files on 2025-11-15, got {date_counter.get('2025-11-15', 0)}"
    
    # Test month aggregation
    month_counter = defaultdict(int)
    for day_key in date_counter:
        try:
            dt = datetime.strptime(day_key, "%Y-%m-%d")
            year_month_key = dt.strftime("%Y-%m")
            month_counter[year_month_key] += date_counter[day_key]
        except (ValueError, AttributeError):
            continue
    
    assert month_counter["2025-12"] == 3, f"Expected 3 files in 2025-12, got {month_counter.get('2025-12', 0)}"
    assert month_counter["2025-11"] == 2, f"Expected 2 files in 2025-11, got {month_counter.get('2025-11', 0)}"
    
    print("✓ All date parsing tests passed!")
    print(f"  Day aggregation: {dict(date_counter)}")
    print(f"  Month aggregation: {dict(month_counter)}")

