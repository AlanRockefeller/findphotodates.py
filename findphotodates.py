class _RateTracker:
    """Tracks processing rate over a rolling window for smooth ETA."""

    def __init__(self, window_seconds=30.0):
        self._window = window_seconds
        self._samples = []  # list of (timestamp, cumulative_count)

    def record(self, timestamp, count):
        self._samples.append((timestamp, count))
        # Trim samples older than window (keep one before window for interpolation)
        cutoff = timestamp - self._window
        first_in_window = 0
        for i, (t, _) in enumerate(self._samples):
            if t >= cutoff:
                first_in_window = max(0, i - 1)
                break
        if first_in_window > 0:
            self._samples = self._samples[first_in_window:]

    def rate(self):
        """Return files/sec over the rolling window, or None if insufficient data."""
        if len(self._samples) < 2:
            return None
        t0, c0 = self._samples[0]
        t1, c1 = self._samples[-1]
        dt = t1 - t0
        if dt < 1.0:
            return None
        return (c1 - c0) / dt


def _format_eta(remaining_files, rate):
    """Format ETA string from remaining files and rate. Returns None if not computable."""
    if rate is None or rate <= 0 or remaining_files <= 0:
        return None
    secs = remaining_files / rate
    if secs > 86400:
        days = int(secs // 86400)
        hours = int((secs % 86400) // 3600)
        return f"{days}d {hours}h"
    elif secs > 3600:
        hours = int(secs // 3600)
        minutes = int((secs % 3600) // 60)
        return f"{hours}h {minutes}m"
    elif secs > 60:
        minutes = int(secs // 60)
        s = int(secs % 60)
        return f"{minutes}m {s}s"
    else:
        return f"{int(secs)}s"


