_DISCOVERY_QUEUE_SIZE = 50_000  # bounded queue for large-tree scans


class _DiscoveryState:
    """Shared state between discovery producer thread and consumer."""

    __slots__ = (
        "discovered",
        "done",
        "done_time",
        "error",
        "t_queue_wait",
        "t_walk",
        "total",
    )

    def __init__(self):
        self.discovered = (
            0  # files found so far (written by producer, read by consumer)
        )
        self.total = 0  # final count (valid only after done is set)
        self.done = threading.Event()
        self.error = None  # exception from producer, if any
        self.done_time = 0.0  # wall-clock time when discovery finished
        self.t_walk = 0.0  # time spent inside scandir walk (excl. queue blocking)
        self.t_queue_wait = 0.0  # time spent blocked on queue.put()


def _discovery_producer(file_queue, directory, extensions, state):
    """Producer thread: walk directory, enqueue file tuples, signal done.

    Each queued item is (filepath, is_symlink, size_bytes, mtime_ns).
    For non-symlinks, size/mtime come from the scandir stat cache (free on Linux).
    For symlinks, size/mtime are None — the consumer stats the resolved target.

    Measures two non-overlapping time buckets inside the loop:
      t_walk  — time spent advancing the find_files generator (scandir I/O)
      t_queue — time spent blocked on queue.put() (backpressure from consumer)
    Their sum ≈ producer wall time (minus small loop overhead).
    """
    try:
        count = 0
        t_walk = 0.0
        t_queue = 0.0
        it = iter(find_files(directory, extensions))
        while True:
            tw0 = time.monotonic()
            try:
                item = next(it)  # (filepath, is_symlink, size_bytes, mtime_ns)
            except StopIteration:
                t_walk += time.monotonic() - tw0
                break
            t_walk += time.monotonic() - tw0

            tq0 = time.monotonic()
            file_queue.put(item)  # blocks if queue full
            t_queue += time.monotonic() - tq0

            count += 1
            state.discovered = count  # atomic on CPython (int store)
        state.total = count
        state.t_walk = t_walk
        state.t_queue_wait = t_queue
    except Exception as e:
        state.error = e
    finally:
        state.done_time = time.time()
        state.done.set()
        # Push sentinel so consumer doesn't block forever on get()
        try:
            file_queue.put(None, timeout=5)
        except Exception:
            pass


# ---------------------------------------------------------------------------
# Rolling-rate tracker for ETA
# ---------------------------------------------------------------------------


class _RateTracker:
    """Tracks processing rate over a rolling window for smooth ETA."""

    def __init__(self, window_seconds=30.0):
        self._window = window_seconds
        self._samples = []  # list of (timestamp, cumulative_count)

    def record(self, timestamp, count):
        self._samples.append((timestamp, count))
        # Trim samples older than window (keep one before window for interpolation)
        cutoff = timestamp - self._window
        first_in_window = 0
        for i, (t, _) in enumerate(self._samples):
            if t >= cutoff:
                first_in_window = max(0, i - 1)
                break
        if first_in_window > 0:
            self._samples = self._samples[first_in_window:]

    def rate(self):
        """Return files/sec over the rolling window, or None if insufficient data."""
        if len(self._samples) < 2:
            return None
        t0, c0 = self._samples[0]
        t1, c1 = self._samples[-1]
        dt = t1 - t0
        if dt < 1.0:
            return None
        return (c1 - c0) / dt


def _format_eta(remaining_files, rate):
    """Format ETA string from remaining files and rate. Returns None if not computable."""
    if rate is None or rate <= 0 or remaining_files <= 0:
        return None
    secs = remaining_files / rate
    if secs > 86400:
        days = int(secs // 86400)
        hours = int((secs % 86400) // 3600)
        return f"{days}d {hours}h"
    elif secs > 3600:
        hours = int(secs // 3600)
        minutes = int((secs % 3600) // 60)
        return f"{hours}h {minutes}m"
    elif secs > 60:
        minutes = int(secs // 60)
        s = int(secs % 60)
        return f"{minutes}m {s}s"
    else:
        return f"{int(secs)}s"


