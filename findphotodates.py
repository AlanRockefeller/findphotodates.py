CONFIG_FILE = Path.home() / ".findphotodates.config.json"

def load_config():
    """Load configuration from JSON file."""
    if not CONFIG_FILE.exists():
        return {"_comment": "Saved scan configurations for findphotodates.py. Edit cautiously; JSON comments are stored here.", "saved_scans": []}
    
    try:
        with open(CONFIG_FILE, 'r', encoding='utf-8') as f:
            config = json.load(f)
        # Ensure required structure
        if "saved_scans" not in config:
            config["saved_scans"] = []
        return config
    except json.JSONDecodeError as e:
        print(f"Error: Invalid JSON in config file {CONFIG_FILE}: {e}")
        print("Please fix or remove the config file and try again.")
        exit(1)
    except Exception as e:
        print(f"Error: Failed to read config file {CONFIG_FILE}: {e}")
        exit(1)

def save_config(config):
    """Save configuration to JSON file atomically."""
    try:
        # Ensure directory exists
        CONFIG_FILE.parent.mkdir(parents=True, exist_ok=True)
        
        # Create temp file in same directory
        fd, temp_file = tempfile.mkstemp(dir=CONFIG_FILE.parent, suffix='.json', prefix='.findphotodates_')
        
        try:
            with os.fdopen(fd, 'w', encoding='utf-8') as f:
                json.dump(config, f, indent=2, ensure_ascii=False, sort_keys=True)
            
            # Atomically replace
            os.replace(temp_file, CONFIG_FILE)
        except Exception:
            try:
                os.unlink(temp_file)
            except Exception:
                pass
            raise
    except Exception as e:
        print(f"Error: Failed to write config file {CONFIG_FILE}: {e}")
        exit(1)

def detect_drive_root(directory_abs):
    """Detect the drive root for a given absolute directory path.
    
    Returns (drive_root, path_relative) where:
    - drive_root is the mount point (e.g., /Volumes/MyPassport, C:\\, /media/user/drive)
    - path_relative is the path relative to drive_root (e.g., "Photos", "DCIM/100CANON")
    
    Best-effort detection using heuristics.
    """
    directory_abs = os.path.abspath(directory_abs)
    system = platform.system()
    
    # Try to find mount points
    if system == "Darwin":  # macOS
        # Check if under /Volumes
        if directory_abs.startswith("/Volumes/"):
            parts = directory_abs.split("/")
            if len(parts) >= 3:
                drive_root = "/" + "/".join(parts[1:3])  # /Volumes/VolumeName
                path_relative = "/".join(parts[3:]) if len(parts) > 3 else ""
                return drive_root, path_relative
    
    elif system == "Linux":
        # Check /media (typically /media/<user>/<drive> - two levels deep)
        if directory_abs.startswith("/media/"):
            parts = directory_abs.split("/")
            # /media/<user>/<drive> - drive is at index 3
            if len(parts) >= 4:
                drive_root = "/" + "/".join(parts[1:4])  # /media/user/drive
                path_relative = "/".join(parts[4:]) if len(parts) > 4 else ""
                return drive_root, path_relative
        
        # Check /run/media (typically /run/media/<user>/<drive> - two levels deep)
        if directory_abs.startswith("/run/media/"):
            parts = directory_abs.split("/")
            # /run/media/<user>/<drive> - drive is at index 4
            if len(parts) >= 5:
                drive_root = "/" + "/".join(parts[1:5])  # /run/media/user/drive
                path_relative = "/".join(parts[5:]) if len(parts) > 5 else ""
                return drive_root, path_relative
        
        # Check /mnt (typically /mnt/<drive> - one level deep)
        if directory_abs.startswith("/mnt/"):
            parts = directory_abs.split("/")
            # /mnt/<drive> - drive is at index 2
            if len(parts) >= 3:
                drive_root = "/" + "/".join(parts[1:3])  # /mnt/drive
                path_relative = "/".join(parts[3:]) if len(parts) > 3 else ""
                return drive_root, path_relative
    
    elif system == "Windows":
        # On Windows, drive root is typically C:\, D:\, etc.
        if len(directory_abs) >= 2 and directory_abs[1] == ":":
            drive_root = directory_abs[:2] + "\\"
            path_relative = directory_abs[3:].replace("\\", "/") if len(directory_abs) > 2 else ""
            return drive_root, path_relative
    
    # Fallback: use directory itself as root
    return directory_abs, ""

def get_drive_hint(drive_root):
    """Extract a hint value from drive root (e.g., volume name)."""
    if not drive_root:
        return ""
    
    # Special case for Windows drive roots (C:\, D:\, etc.)
    if platform.system() == "Windows" and len(drive_root) >= 2 and drive_root[1] == ":":
        # Use os.path.splitdrive to get just the letter
        drive, _ = os.path.splitdrive(drive_root)
        return drive.rstrip(":")  # Return just "C", "D", etc.
    
    # Extract last component
    if os.path.sep in drive_root:
        parts = drive_root.rstrip(os.path.sep).split(os.path.sep)
        return parts[-1] if parts else ""
    elif ":" in drive_root:
        # Windows drive letter (fallback)
        return drive_root.rstrip(":\\")
    return drive_root

def resolve_saved_directory(entry):
    """Resolve a saved scan entry to a current directory path.
    
    Returns the resolved directory path, or None if not found.
    """
    # Try directory_abs first
    if entry.get("directory_abs") and os.path.exists(entry["directory_abs"]):
        return entry["directory_abs"]
    
    # Try to reconstruct using path_relative + drive root detection
    path_relative = entry.get("path_relative", "")
    drive_hint = entry.get("drive_hint", {}).get("value", "")
    
    system = platform.system()
    candidate_roots = []
    
    if system == "Darwin":  # macOS
        volumes_dir = Path("/Volumes")
        if volumes_dir.exists():
            for vol in volumes_dir.iterdir():
                if vol.is_dir() and (not drive_hint or vol.name == drive_hint):
                    candidate_roots.append(str(vol))
    
    elif system == "Linux":
        # Handle /media/<user>/<drive> (two levels deep)
        media_path = Path("/media")
        if media_path.exists():
            for user_dir in media_path.iterdir():
                if user_dir.is_dir():
                    for vol in user_dir.iterdir():
                        if vol.is_dir() and (not drive_hint or vol.name == drive_hint):
                            candidate_roots.append(str(vol))
        
        # Handle /run/media/<user>/<drive> (two levels deep)
        run_media_path = Path("/run/media")
        if run_media_path.exists():
            for user_dir in run_media_path.iterdir():
                if user_dir.is_dir():
                    for vol in user_dir.iterdir():
                        if vol.is_dir() and (not drive_hint or vol.name == drive_hint):
                            candidate_roots.append(str(vol))
        
        # Handle /mnt/<drive> (one level deep)
        mnt_path = Path("/mnt")
        if mnt_path.exists():
            for vol in mnt_path.iterdir():
                if vol.is_dir() and (not drive_hint or vol.name == drive_hint):
                    candidate_roots.append(str(vol))
    
    elif system == "Windows":
        # Check all drive letters
        for letter in string.ascii_uppercase:
            drive = f"{letter}:\\"
            if os.path.exists(drive):
                if not drive_hint or drive_hint.upper() == letter:
                    candidate_roots.append(drive)
    
    # Try each candidate root
    for root in candidate_roots:
        if path_relative:
            # Normalize path separators for current platform
            normalized_relative = path_relative.replace("/", os.path.sep).replace("\\", os.path.sep)
            candidate = os.path.join(root, normalized_relative)
        else:
            candidate = root
        
        if os.path.exists(candidate) and os.path.isdir(candidate):
            return candidate
    
    # Fallback: try directory_abs even if it doesn't exist (might be temporarily unmounted)
    return entry.get("directory_abs")

def run_scan(directory, output, extensions, locate=False, quiet=False, debug=False):
    """Run a scan on the specified directory.
    
    Args:
        directory: Directory to scan
        output: Output TSV file path
        extensions: Set of file extensions to scan
        locate: Whether to geolocate files
        quiet: Whether to suppress output
        debug: Whether to enable debug mode
    
    Returns:
        True if successful, False otherwise
    """
    # Ensure the directory exists
    if not os.path.exists(directory):
        if not quiet:
            print(f"Error: Directory '{directory}' does not exist.")
        return False

    if not quiet:
        print(f"Searching for files in '{directory}' with extensions: {', '.join(extensions)}...")

    files = list(find_files(directory, extensions))

    if not files:
        if not quiet:
            print("No files found with the specified extensions.")
        return True  # Not an error, just no files

    if not quiet:
        print(f"Found {len(files)} files.")

    file_counts = Counter(file.split(".")[-1].lower() for file in files)

    # Load existing cache
    cache = load_cache(output)
    if not quiet and cache:
        print(f"Loaded {len(cache)} entries from cache.")

    # Redirect warnings to a unique log file per run
    error_log_path = os.path.join(
        tempfile.gettempdir(),
        f"findphotodates.error.{os.getpid()}.{int(time.time())}.log"
    )
    photo_data = []
    files_to_process = []
    cached_count = 0
    
    with open(error_log_path, "w") as error_log:
        
        for idx, file in enumerate(files):
            try:
                # Preserve what user sees in output (abspath)
                out_path = os.path.abspath(file)
                # Use canonical path for cache key (realpath) to dedupe symlinks
                key_path = os.path.realpath(out_path)
                
                # Get filesystem metadata using canonical path
                stat_info = os.stat(key_path)
                size_bytes = stat_info.st_size
                mtime_ns = stat_info.st_mtime_ns
                
                # Check cache using canonical path
                cached_entry = cache.get(key_path)
                if cached_entry and cached_entry['size_bytes'] == size_bytes and cached_entry['mtime_ns'] == mtime_ns:
                    # Use cached data
                    date_taken = cached_entry['date_taken']
                    gps_lat = cached_entry.get('gps_lat')
                    gps_lon = cached_entry.get('gps_lon')
                    location = cached_entry.get('location')
                    
                    # If --locate is requested but location is missing, try to geolocate from cached GPS
                    # Note: This location will be persisted when we write photo_data to the TSV
                    # Also retry if location looks like raw coordinates (treat as missing)
                    if locate and gps_lat and gps_lon:
                        if not location or is_coordinate_string(location):
                            location = geolocate(gps_lat, gps_lon)
                            # Update cached location for this entry (will be written back via photo_data)
                            cached_entry['location'] = location
                    
                    if date_taken:
                        # Always preserve location if it exists (don't delete on non--locate runs)
                        # Use out_path (abspath) for output to preserve what user sees
                        photo_data.append((out_path, date_taken, size_bytes, mtime_ns, gps_lat, gps_lon, location))
                        cached_count += 1
                    else:
                        error_log.write(f"Warning: Cached entry for '{out_path}' has no date.\n")
                else:
                    # File is new or changed, needs processing
                    # Store both paths: out_path for output, key_path for cache lookup
                    files_to_process.append((out_path, key_path, size_bytes, mtime_ns))
            except OSError as e:
                error_log.write(f"Warning: Could not stat '{file}': {str(e)}\n")
                # Check for drive disconnect
                if any(err in str(e).lower() for err in ['no such file', 'input/output error', 'stale file handle']):
                    print("\n\nWARNING: Drive may be disconnected! Saving progress...")
                    write_dates_to_file_atomic(output, photo_data)
                    print(f"Saved {len(photo_data)} files to '{output}'")
                    print("Reconnect drive and run again to resume.")
                    return False
                continue

        if not quiet:
            print(f"Found {cached_count} files in cache, {len(files_to_process)} files need processing.")

        # Estimate runtime and save results from first files
        start_time = time.time()
        last_save_time = start_time
        estimated_finish_time = None
        timing_results = []  # Save results from timing test files
        start_idx = 0
        
        if len(files_to_process) > 10 and not quiet:
            test_files = min(5, len(files_to_process))
            for out_path, key_path, size_bytes, mtime_ns in files_to_process[:test_files]:
                try:
                    # Always extract GPS data (even if locate=False) for caching
                    # Use key_path (canonical) for exiftool, but out_path for output
                    date_taken, gps_lat, gps_lon = get_photo_data(key_path)
                    location = None
                    if locate and gps_lat and gps_lon:
                        location = geolocate(gps_lat, gps_lon)
                        # Don't save coordinate strings as location (treat as None to allow retry)
                        if location and is_coordinate_string(location):
                            location = None
                    if date_taken:
                        # Save result for reuse - use out_path for output
                        timing_results.append((out_path, date_taken, size_bytes, mtime_ns, gps_lat, gps_lon, location))
                    else:
                        error_log.write(f"Warning: Could not extract date for '{out_path}'.\n")
                except OSError as e:
                    error_log.write(f"Error: Could not access '{out_path}': {e}\n")
                    # Check for drive disconnect
                    if any(err in str(e).lower() for err in ['no such file', 'input/output error', 'stale file handle']):
                        print("\n\nWARNING: Drive may be disconnected! Saving progress...")
                        write_dates_to_file_atomic(output, photo_data)
                        print(f"Saved {len(photo_data)} files to '{output}'")
                        print("Reconnect drive and run again to resume.")
                        return False
                    continue
            
            elapsed_time = time.time() - start_time
            avg_time_per_file = elapsed_time / test_files
            estimated_total_time = avg_time_per_file * len(files_to_process)
            estimated_finish_time = datetime.now() + timedelta(seconds=estimated_total_time)

            if debug:
                print(f"Timing the processing of the first {test_files} files to estimate total runtime...")
            if estimated_total_time > 3600:
                hours = int(estimated_total_time // 3600)
                minutes = int((estimated_total_time % 3600) // 60)
                print(f"Estimated total runtime: {hours} hours and {minutes} minutes.")
            elif estimated_total_time > 180:
                minutes = int(estimated_total_time // 60)
                seconds = int(estimated_total_time % 60)
                print(f"Estimated total runtime: {minutes} minutes and {seconds} seconds.")
            else:
                print(f"Estimated total runtime: {estimated_total_time:.2f} seconds.")
            print(f"Expected to finish by: {estimated_finish_time.strftime('%Y-%m-%d %H:%M:%S')}.")
            
            # Start main processing from index test_files (skip files we already processed)
            start_idx = test_files
            # Prepend saved results to photo_data
            photo_data.extend(timing_results)

        # Process remaining files that need exiftool
        for idx, (out_path, key_path, size_bytes, mtime_ns) in enumerate(files_to_process[start_idx:], start=start_idx):
            try:
                # Always extract GPS data (even if locate=False) for caching
                # Use key_path (canonical) for exiftool, but out_path for output
                date_taken, gps_lat, gps_lon = get_photo_data(key_path)
                location = None
                if locate and gps_lat and gps_lon:
                    location = geolocate(gps_lat, gps_lon)
                    # Don't save coordinate strings as location (treat as None to allow retry)
                    if location and is_coordinate_string(location):
                        location = None
                if date_taken:
                    # Always preserve location if computed (for future --locate runs)
                    # Use out_path (abspath) for output to preserve what user sees
                    photo_data.append((out_path, date_taken, size_bytes, mtime_ns, gps_lat, gps_lon, location))
                else:
                    error_log.write(f"Warning: Could not extract date for '{out_path}'.\n")
            except OSError as e:
                error_log.write(f"Error: Could not access '{out_path}': {e}\n")
                # Check for drive disconnect
                if any(err in str(e).lower() for err in ['no such file', 'input/output error', 'stale file handle']):
                    print("\n\nWARNING: Drive may be disconnected! Saving progress...")
                    write_dates_to_file_atomic(output, photo_data)
                    print(f"Saved {len(photo_data)} files to '{output}'")
                    print("Reconnect drive and run again to resume.")
                    return False
                continue

            # Save checkpoint every 100 files or 5 minutes
            current_time = time.time()
            if (idx + 1) % 100 == 0 or (current_time - last_save_time) > 300:
                write_dates_to_file_atomic(output, photo_data)
                last_save_time = current_time
                if not quiet:
                    print(f"\n[Checkpoint: {len(photo_data)} files saved]", end="")

            # Update progress bar after processing (display after, not before)
            if not quiet and idx % 10 == 0:  # Update less frequently for better performance
                # Use idx + 1 since we display after processing (idx is 0-based, we've processed idx+1 files)
                progress_percentage = (idx + 1) / len(files_to_process) * 100
                progress_bar = '[' + '#' * int(progress_percentage // 5) + ' ' * (20 - int(progress_percentage // 5)) + ']'
                print(f"Progress: {progress_percentage:.2f}% {progress_bar}", end="\r")

    if not quiet:
        print("\nProcessing complete.                                        ")  # Extra spaces to clear progress bar

    # Write results to the output file atomically
    write_dates_to_file_atomic(output, photo_data)

    if not quiet:
        print(f"Dates written to '{output}' ({len(photo_data)} files listed).")
        if cached_count > 0:
            print(f"  ({cached_count} from cache, {len(photo_data) - cached_count} newly processed)")

    # Print summary of results
    summarize_results(photo_data, file_counts, quiet, debug)
    
    return True

