def detect_drive_root(directory_abs):
    """Detect the drive root for a given absolute directory path."""
    directory_abs = os.path.abspath(directory_abs)
    system = platform.system()

    # Try to find mount points
    if system == "Darwin":  # macOS
        # Check if under /Volumes
        if directory_abs.startswith("/Volumes/"):
            parts = directory_abs.split("/")
            if len(parts) >= 3:
                drive_root = "/" + "/".join(parts[1:3])  # /Volumes/VolumeName
                path_relative = "/".join(parts[3:]) if len(parts) > 3 else ""
                return drive_root, path_relative

    elif system == "Linux":
        # Check /media (typically /media/<user>/<drive> - two levels deep)
        if directory_abs.startswith("/media/"):
            parts = directory_abs.split("/")
            # /media/<user>/<drive> - drive is at index 3
            if len(parts) >= 4:
                drive_root = "/" + "/".join(parts[1:4])  # /media/user/drive
                path_relative = "/".join(parts[4:]) if len(parts) > 4 else ""
                return drive_root, path_relative

        # Check /run/media (typically /run/media/<user>/<drive> - two levels deep)
        if directory_abs.startswith("/run/media/"):
            parts = directory_abs.split("/")
            # /run/media/<user>/<drive> - drive is at index 4
            if len(parts) >= 5:
                drive_root = "/" + "/".join(parts[1:5])  # /run/media/user/drive
                path_relative = "/".join(parts[5:]) if len(parts) > 5 else ""
                return drive_root, path_relative

        # Check /mnt (typically /mnt/<drive> - one level deep)
        if directory_abs.startswith("/mnt/"):
            parts = directory_abs.split("/")
            # /mnt/<drive> - drive is at index 2
            if len(parts) >= 3:
                drive_root = "/" + "/".join(parts[1:3])  # /mnt/drive
                path_relative = "/".join(parts[3:]) if len(parts) > 3 else ""
                return drive_root, path_relative

    elif system == "Windows":
        # On Windows, drive root is typically C:\, D:\, etc.
        if len(directory_abs) >= 2 and directory_abs[1] == ":":
            drive_root = directory_abs[:2] + "\\"
            path_relative = (
                directory_abs[3:].replace("\\", "/") if len(directory_abs) > 2 else ""
            )
            return drive_root, path_relative

    # Fallback: use directory itself as root
    return directory_abs, ""


def get_drive_hint(drive_root):
    """Extract a hint value from drive root (e.g., volume name)."""
    if not drive_root:
        return ""

    # Special case for Windows drive roots (C:\, D:\, etc.)
    if platform.system() == "Windows" and len(drive_root) >= 2 and drive_root[1] == ":":
        # Use os.path.splitdrive to get just the letter
        drive, _ = os.path.splitdrive(drive_root)
        return drive.rstrip(":")  # Return just "C", "D", etc.

    # Extract last component
    if os.path.sep in drive_root:
        parts = drive_root.rstrip(os.path.sep).split(os.path.sep)
        return parts[-1] if parts else ""
    elif ":" in drive_root:
        # Windows drive letter (fallback)
        return drive_root.rstrip(":\\")
    return drive_root


def resolve_saved_directory(entry):
    """Resolve a saved scan entry to a current directory path."""
    # Try directory_abs first
    if entry.get("directory_abs") and os.path.exists(entry["directory_abs"]):
        return entry["directory_abs"]

    # Try to reconstruct using path_relative + drive root detection
    path_relative = entry.get("path_relative", "")
    drive_hint = entry.get("drive_hint", {}).get("value", "")

    system = platform.system()
    candidate_roots = []

    if system == "Darwin":  # macOS
        volumes_dir = Path("/Volumes")
        if volumes_dir.exists():
            for vol in volumes_dir.iterdir():
                if vol.is_dir() and (not drive_hint or vol.name == drive_hint):
                    candidate_roots.append(str(vol))

    elif system == "Linux":
        # Handle /media/<user>/<drive> (two levels deep)
        media_path = Path("/media")
        if media_path.exists():
            for user_dir in media_path.iterdir():
                if user_dir.is_dir():
                    for vol in user_dir.iterdir():
                        if vol.is_dir() and (not drive_hint or vol.name == drive_hint):
                            candidate_roots.append(str(vol))

        # Handle /run/media/<user>/<drive> (two levels deep)
        run_media_path = Path("/run/media")
        if run_media_path.exists():
            for user_dir in run_media_path.iterdir():
                if user_dir.is_dir():
                    for vol in user_dir.iterdir():
                        if vol.is_dir() and (not drive_hint or vol.name == drive_hint):
                            candidate_roots.append(str(vol))

        # Handle /mnt/<drive> (one level deep)
        mnt_path = Path("/mnt")
        if mnt_path.exists():
            for vol in mnt_path.iterdir():
                if vol.is_dir() and (not drive_hint or vol.name == drive_hint):
                    candidate_roots.append(str(vol))

    elif system == "Windows":
        # Check all drive letters
        for letter in string.ascii_uppercase:
            drive = f"{letter}:\\"
            if os.path.exists(drive):
                if not drive_hint or drive_hint.upper() == letter:
                    candidate_roots.append(drive)

    # Try each candidate root
    for root in candidate_roots:
        if path_relative:
            # Normalize path separators for current platform
            normalized_relative = path_relative.replace("/", os.path.sep).replace(
                "\\", os.path.sep
            )
            candidate = os.path.join(root, normalized_relative)
        else:
            candidate = root

        if os.path.exists(candidate) and os.path.isdir(candidate):
            return candidate

    # Fallback: try directory_abs even if it doesn't exist (might be temporarily unmounted)
    return entry.get("directory_abs")


def _format_duration(seconds):
    """Format seconds into a human-readable duration string."""
    if seconds > 3600:
        hours = int(seconds // 3600)
        minutes = int((seconds % 3600) // 60)
        return f"{hours}h {minutes}m"
    elif seconds > 60:
        minutes = int(seconds // 60)
        secs = int(seconds % 60)
        return f"{minutes}m {secs}s"
    else:
        return f"{seconds:.1f}s"


def _check_drive_disconnect(
    error,
    output,
    photo_data,
    inventory_root,
    hash_options,
    old_format,
    cleanup_fn,
    path_style="linux",  # safe internal default; CLI default is "auto" (resolved before calling)
):
    """Check for drive disconnect errors and save progress. Returns True if disconnect detected."""
    if any(
        err in str(error).lower()
        for err in ["no such file", "input/output error", "stale file handle"]
    ):
        print("\n\nWARNING: Drive may be disconnected! Saving progress...")
        write_dates_to_file_atomic(
            output,
            photo_data,
            inventory_root=inventory_root,
            hash_options=hash_options,
            old_format=old_format,
            path_style=path_style,
        )
        print(f"Saved {len(photo_data)} files to '{output}'")
        print("Reconnect drive and run again to resume.")
        cleanup_fn()
        return True
    return False


