# ---------------------------------------------------------------------------
# Cross-platform cache-key normalization (WSL ↔ Windows)
# ---------------------------------------------------------------------------
# WSL mount paths like /mnt/f/Photos and Windows paths like F:\Photos refer
# to the same file.  We normalize both forms to a lowercase-drive forward-slash
# canonical key so that inventories created under one environment get cache
# hits when re-read under the other.
#
# Canonical form:  /mnt/<drive>/<rest-with-forward-slashes>
#   /mnt/f/Documents/file.jpg  →  /mnt/f/Documents/file.jpg   (unchanged)
#   F:\Documents\file.jpg      →  /mnt/f/Documents/file.jpg
#
# Non-drive paths (Linux home dirs, UNC paths, etc.) pass through unchanged
# after backslash → forward-slash conversion.

_WSL_MOUNT_RE = re.compile(r"^/mnt/([a-zA-Z])/")
_WIN_DRIVE_RE = re.compile(r"^([a-zA-Z]):[/\\]")

_QUOTE_TABLE = str.maketrans(
    {
        "\u2018": "'",  # Left single quotation mark
        "\u2019": "'",  # Right single quotation mark
        "\u2032": "'",  # Prime
        "\u201a": "'",  # Single low-9 quote
        "\u0060": "'",  # Backtick
        "\uf027": "'",  # WSL private-use single quote
        "\u201c": '"',  # Left double quotation mark
        "\u201d": '"',  # Right double quotation mark
        "\u2033": '"',  # Double prime
        "\u201e": '"',  # Double low-9 quote
        "\u00ab": '"',  # Guillemet left
        "\u00bb": '"',  # Guillemet right
        "\uf022": '"',  # WSL private-use double quote mapping
    }
)
# Fallback sweep: strip any other codepoints in the Private Use Area range U+F000 to U+F0FF
for _cp in range(0xF000, 0xF100):
    if _cp not in _QUOTE_TABLE:
        _QUOTE_TABLE[_cp] = None


def _normalize_cache_key(path):
    """Normalize a filepath into a cross-platform canonical cache key.

    Maps Windows drive paths (F:\\...) and WSL mount paths (/mnt/f/...) to a
    shared canonical form so the same physical file gets the same key regardless
    of whether the inventory was created under WSL or native Windows.
    """
    # 1. Normalize Unicode composition
    p = unicodedata.normalize("NFC", path)

    # 2. Map known problematic quote variants and strip undefined GUI/PUA glyphs
    p = p.translate(_QUOTE_TABLE)

    # 3. Normalize backslashes to forward slashes first
    p = p.replace("\\", "/")

    # Windows drive letter  →  /mnt/<lower-drive>/rest
    m = _WIN_DRIVE_RE.match(p)
    if m:
        drive = m.group(1).lower()
        rest = p[3:]  # skip  X:/
        return f"/mnt/{drive}/{rest}"

    # WSL mount  →  ensure lowercase drive letter
    m = _WSL_MOUNT_RE.match(p)
    if m:
        drive = m.group(1).lower()
        return f"/mnt/{drive}/{p[len(m.group(0)) :]}"

    return p


def format_path_style(path, style):
    """Convert paths to the specified style (linux or windows)."""
    if style == "linux":
        path = path.replace("\\", "/")
        m = _WIN_DRIVE_RE.match(path)
        if m:
            drive = m.group(1).lower()
            rest = path[3:]
            path = f"/mnt/{drive}" + (f"/{rest}" if rest else "")
    elif style == "windows":
        m = _WSL_MOUNT_RE.match(path)
        if m:
            drive = m.group(1).upper()
            rest = path[len(m.group(0)) :]
            path = f"{drive}:\\" + rest if rest else f"{drive}:\\"
        elif (
            path.startswith("/mnt/")
            and len(path) == 6
            and path[5].isalpha()
            and path[4] == "/"
        ):
            path = f"{path[5].upper()}:\\"
        path = path.replace("/", "\\")
    return path


def _host_path_style():
    """Return the current platform's natural path style."""
    if platform.system() == "Windows":
        return "windows"
    return "linux"


def _resolve_local_inventory_path(path):
    """Convert a stored inventory filepath into a host-resolvable local path."""
    if not path:
        return path
    return os.path.abspath(format_path_style(path, _host_path_style()))


def detect_inventory_path_style(tsv_path):
    """Detect the path style used in an existing inventory file.

    Checks the ``# inventory_root=`` comment first, then falls back to the
    first data row's filepath column.  Returns ``"linux"``, ``"windows"``,
    or ``None`` if the file is empty / unreadable / style cannot be determined.
    """
    try:
        with open(tsv_path, "r", encoding="utf-8") as f:
            past_header = False
            for line in f:
                line = line.rstrip("\n\r")
                if not line:
                    continue
                if line.startswith("# inventory_root="):
                    sample = line.split("=", 1)[1]
                    return _detect_style_from_path(sample)
                if line.startswith("#"):
                    continue
                if not past_header:
                    # This is the TSV column header row — skip it
                    past_header = True
                    continue
                # First data row — extract the filepath field
                parts = line.split("\t", 1)
                if parts:
                    return _detect_style_from_path(parts[0])
    except (IOError, UnicodeDecodeError):
        pass
    return None


def _detect_style_from_path(sample):
    """Return ``"windows"`` or ``"linux"`` based on a single path string."""
    if _WIN_DRIVE_RE.match(sample) or "\\" in sample:
        return "windows"
    if sample.startswith("/"):
        return "linux"
    return None


def resolve_output_path_style(requested_style, output_file, directory):
    """Resolve the effective path style for inventory output.

    *requested_style* is ``"auto"``, ``"linux"``, or ``"windows"``.

    Resolution order for ``"auto"``:
    1. If *output_file* already exists, preserve its detected style.
    2. If *directory* looks like a Windows path (``C:\\...``), use ``"windows"``.
    3. If *directory* looks like a WSL mount (``/mnt/c/...``), use ``"linux"``.
    4. Fall back to the current platform's natural style.
    """
    if requested_style in ("linux", "windows"):
        return requested_style

    # Auto mode — try to preserve existing inventory style
    if output_file and os.path.exists(output_file):
        detected = detect_inventory_path_style(output_file)
        if detected:
            return detected

    # Infer from the scan directory
    if directory:
        dir_style = _detect_style_from_path(directory)
        if dir_style:
            return dir_style

    # Platform fallback
    if platform.system() == "Windows":
        return "windows"
    return "linux"


def parse_hash_args(
    hash_mode="off",
    sample_chunks=3,
    sample_chunk_mib=0.0625,
    hash_algo=None,
    hash_cache_path=None,
    no_hash_cache=False,
    hash_exts_str=None,
    quiet=False,
):
    """Parse and normalize hashing CLI options (unit-testable)."""
    mode = (hash_mode or "off").lower()
    if mode not in ("sample", "full", "off"):
        mode = "off"
    chunks = max(1, int(sample_chunks))
    chunk_mib = max(0.001, float(sample_chunk_mib))
    algo_override = None
    if hash_algo:
        algo_override = hash_algo.lower()
        if algo_override not in ("blake3", "blake2b", "sha256"):
            algo_override = None
    use_cache = not no_hash_cache
    path = hash_cache_path
    if use_cache and path is None:
        path = str(DEFAULT_HASH_CACHE_PATH)
    hash_exts = None
    if hash_exts_str and hash_exts_str.strip():
        hash_exts = frozenset(
            ext.strip().lower().lstrip(".")
            for ext in hash_exts_str.split(",")
            if ext.strip()
        )

    # Resolve algo for sample: prefer blake3, else blake2b (or sha256 if requested)
    if algo_override == "sha256":
        sample_algo = "sha256"
    elif _BLAKE3_AVAILABLE and (algo_override is None or algo_override == "blake3"):
        sample_algo = "blake3"
    elif algo_override == "blake2b":
        sample_algo = "blake2b"
    else:
        sample_algo = "blake2b"  # stdlib fallback

    # Resolve algo for full: blake3 or sha256
    if _BLAKE3_AVAILABLE and (algo_override is None or algo_override == "blake3"):
        full_algo = "blake3"
    elif algo_override == "sha256":
        full_algo = "sha256"
    elif algo_override == "blake2b":
        if not quiet:
            print(
                "Warning: blake2b is not supported for full hashes; using sha256 instead."
            )
        full_algo = "sha256"
    else:
        full_algo = "sha256"

    return types.SimpleNamespace(
        hash_mode=mode,
        sample_chunks=chunks,
        sample_chunk_mib=chunk_mib,
        hash_algo_override=algo_override,
        use_hash_cache=use_cache,
        hash_cache_path=path,
        hash_exts=hash_exts,
        sample_algo=sample_algo,
        full_algo=full_algo,
    )


def compute_samplehash_v1(path, chunk_bytes, num_chunks, algo="blake3"):
    """Compute a fast sample fingerprint from path (size + \0 + chunks).

    For small files (size <= num_chunks * chunk_bytes), hashes the whole file.
    Supports blake3, blake2b(digest_size=16), or sha256.
    Returns hex digest string or None on error.
    """
    try:
        st = os.stat(path)
        size = st.st_size
    except OSError:
        return None

    if algo == "blake3" and _BLAKE3_AVAILABLE:
        h = blake3.blake3()
    elif algo == "sha256":
        h = hashlib.sha256()
    else:
        h = hashlib.blake2b(digest_size=16)

    h.update(str(size).encode("ascii"))
    h.update(b"\0")

    total_sample_bytes = num_chunks * chunk_bytes
    small_file = size <= total_sample_bytes or size == 0

    try:
        with open(path, "rb") as f:
            if small_file:
                # Stream the entire file in chunks to avoid memory blow-up
                for data in iter(lambda: f.read(65536), b""):
                    h.update(data)
            else:
                # Distribute offsets: first 0, last size-chunk_bytes, middle ones spread evenly
                chunks_to_read = []
                end_offset = max(0, size - chunk_bytes)
                for i in range(num_chunks):
                    if i == 0:
                        offset = 0
                    elif i == num_chunks - 1:
                        offset = end_offset
                    else:
                        offset = (
                            (i * end_offset) // (num_chunks - 1)
                            if num_chunks > 1
                            else 0
                        )
                    offset = max(0, min(offset, end_offset))
                    chunks_to_read.append(offset)

                # Deduplicate and sort offsets
                for offset in sorted(set(chunks_to_read)):
                    f.seek(offset)
                    data = f.read(chunk_bytes)
                    if data:
                        h.update(data)
        return h.hexdigest()
    except OSError:
        return None


def compute_fullhash(path, algo="blake3"):
    """Compute full-file hash. algo is 'blake3', 'blake2b', or 'sha256'. Returns hex digest or None."""
    try:
        if algo == "blake3" and _BLAKE3_AVAILABLE:
            h = blake3.blake3()
        elif algo == "blake2b":
            h = hashlib.blake2b()
        elif algo in ("sha256", "blake3"):
            # blake3 requested but not available — fall back to sha256
            h = hashlib.sha256()
        else:
            print(
                f"WARNING: Unknown full-hash algo {algo!r}, falling back to sha256",
                file=sys.stderr,
            )
            h = hashlib.sha256()
        with open(path, "rb") as f:
            for chunk in iter(lambda: f.read(65536), b""):
                h.update(chunk)
        return h.hexdigest()
    except OSError:
        return None


def open_sqlite_cache(cache_path, debug=False):
    """Create cache dir if needed, open SQLite connection, ensure table exists. Returns connection or None on error."""
    try:
        import sqlite3
    except ImportError:
        if debug:
            print("Debug: sqlite3 module not found.")
        return None
    try:
        Path(cache_path).parent.mkdir(parents=True, exist_ok=True)
        conn = sqlite3.connect(cache_path)
        conn.execute("PRAGMA journal_mode=WAL;")
        conn.execute("PRAGMA synchronous=NORMAL;")
        conn.execute("""
            CREATE TABLE IF NOT EXISTS hash_cache (
                path TEXT NOT NULL,
                size_bytes INTEGER NOT NULL,
                mtime_ns INTEGER NOT NULL,
                hash_mode TEXT NOT NULL,
                chunk_bytes INTEGER NOT NULL,
                chunks INTEGER NOT NULL,
                algo TEXT NOT NULL,
                digest TEXT NOT NULL,
                PRIMARY KEY (path, size_bytes, mtime_ns, hash_mode, chunk_bytes, chunks, algo)
            )
        """)
        conn.commit()
        return conn
    except Exception as e:
        if debug:
            print(f"Debug: Failed to open hash cache at {cache_path}: {e}")
        return None


def get_cached_hash(
    conn, path, size_bytes, mtime_ns, hash_mode, chunk_bytes, chunks, algo
):
    """Return cached digest string if present, else None."""
    if conn is None:
        return None
    try:
        row = conn.execute(
            """SELECT digest FROM hash_cache
               WHERE path = ? AND size_bytes = ? AND mtime_ns = ?
                 AND hash_mode = ? AND chunk_bytes = ? AND chunks = ? AND algo = ?""",
            (path, size_bytes, mtime_ns, hash_mode, chunk_bytes, chunks, algo),
        ).fetchone()
        return row[0] if row else None
    except Exception:
        return None


def put_cached_hash(
    conn,
    path,
    size_bytes,
    mtime_ns,
    hash_mode,
    chunk_bytes,
    chunks,
    algo,
    digest,
    pending_counter=None,
):
    """Store digest in cache. If pending_counter is a list [n], batch-commit every HASH_CACHE_BATCH_COMMIT_EVERY."""
    if conn is None:
        return
    try:
        conn.execute(
            """INSERT OR REPLACE INTO hash_cache
               (path, size_bytes, mtime_ns, hash_mode, chunk_bytes, chunks, algo, digest)
               VALUES (?, ?, ?, ?, ?, ?, ?, ?)""",
            (path, size_bytes, mtime_ns, hash_mode, chunk_bytes, chunks, algo, digest),
        )
        if pending_counter is not None:
            pending_counter[0] += 1
            if pending_counter[0] >= HASH_CACHE_BATCH_COMMIT_EVERY:
                conn.commit()
                pending_counter[0] = 0
        else:
            conn.commit()
    except Exception:
        pass


def get_content_hash(
    key_path,
    size_bytes,
    mtime_ns,
    hash_options,
    hash_conn,
    pending_counter=None,
    os_path=None,
):
    """Return content_hash for a file: from cache or compute. Returns '' if hashing off or restricted by ext.

    key_path is used for cache lookups (normalized for cross-platform reuse).
    os_path is the actual filesystem path for reading (defaults to key_path).
    """
    if hash_options.hash_mode == "off":
        return ""
    ext = (os.path.splitext(key_path)[1] or "").lstrip(".").lower()
    if hash_options.hash_exts is not None and ext not in hash_options.hash_exts:
        return ""
    if os_path is None:
        os_path = key_path
    chunk_bytes = int(hash_options.sample_chunk_mib * 1024 * 1024)
    chunk_bytes = max(1, chunk_bytes)
    if hash_options.hash_mode == "sample":
        cached = get_cached_hash(
            hash_conn,
            key_path,
            size_bytes,
            mtime_ns,
            "sample",
            chunk_bytes,
            hash_options.sample_chunks,
            hash_options.sample_algo,
        )
        if cached is not None:
            return cached
        digest = compute_samplehash_v1(
            os_path,
            chunk_bytes,
            hash_options.sample_chunks,
            algo=hash_options.sample_algo,
        )
        if digest and hash_conn:
            put_cached_hash(
                hash_conn,
                key_path,
                size_bytes,
                mtime_ns,
                "sample",
                chunk_bytes,
                hash_options.sample_chunks,
                hash_options.sample_algo,
                digest,
                pending_counter=pending_counter,
            )
        return digest or ""
    else:
        cached = get_cached_hash(
            hash_conn,
            key_path,
            size_bytes,
            mtime_ns,
            "full",
            0,
            0,
            hash_options.full_algo,
        )
        if cached is not None:
            return cached
        digest = compute_fullhash(os_path, hash_options.full_algo)
        if digest and hash_conn:
            put_cached_hash(
                hash_conn,
                key_path,
                size_bytes,
                mtime_ns,
                "full",
                0,
                0,
                hash_options.full_algo,
                digest,
                pending_counter=pending_counter,
            )
        return digest or ""


def is_supported_file(filename, extensions):
    """Check if a file has a supported extension. If extensions is None, all files match."""
    if extensions is None:
        return True
    return any(filename.lower().endswith(f".{ext}") for ext in extensions)


def find_files(directory, extensions):
    """Recursively find all files with the specified extensions using os.scandir().

    Yields (filepath, is_symlink, size_bytes, mtime_ns) tuples.
    For non-symlink files, size_bytes and mtime_ns come from the DirEntry stat
    cache (no extra syscall on Linux).  For symlinks, they are None so the
    consumer stats the resolved target itself.

    Symlinked directories are NOT recursed into (avoids duplicate traversal and
    infinite loops from symlink cycles).  Symlinked files are still yielded.
    Permission errors and broken paths are silently skipped.
    """
    try:
        scan_it = os.scandir(directory)
    except (PermissionError, OSError):
        return
    with scan_it:
        for entry in scan_it:
            try:
                is_link = entry.is_symlink()
                if entry.is_dir(follow_symlinks=False):
                    # Real directory — recurse
                    yield from find_files(entry.path, extensions)
                elif is_link and entry.is_dir(follow_symlinks=True):
                    # Symlinked directory — skip recursion to avoid
                    # duplicate traversal and symlink loops
                    pass
                elif entry.is_file(follow_symlinks=True):
                    if is_supported_file(entry.name, extensions):
                        if is_link:
                            yield entry.path, True, None, None
                        else:
                            st = entry.stat(follow_symlinks=True)
                            yield entry.path, False, st.st_size, st.st_mtime_ns
            except (PermissionError, OSError):
                continue


