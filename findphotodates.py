class ExifToolPersistent:
    """Persistent exiftool process using -stay_open for high throughput.

    Supports both single-file queries and batch queries (via -json).
    """

    _SENTINEL = "{ready}\n"
    _TAGS = [
        "-DateTimeOriginal",
        "-CreateDate",
        "-MediaCreateDate",
        "-GPSLatitude",
        "-GPSLongitude",
    ]
    _DATE_TAG_COUNT = 3  # first 3 are date tags
    _DATE_KEYS = ("DateTimeOriginal", "CreateDate", "MediaCreateDate")

    def __init__(self):
        self._proc = None

    def start(self):
        if self._proc is not None:
            return
        try:
            # Alan 5/3/26 - Force UTF-8 on the pipes to ExifTool.  Without
            # explicit encoding, Python uses the locale codec for text=True
            # pipes, which on Windows is typically cp1252 and crashes with
            # UnicodeEncodeError on filenames containing characters like
            # U+202F (narrow no-break space).  "-charset filename=UTF8" tells
            # ExifTool that filenames arriving on the -@ argfile pipe are
            # UTF-8 rather than the active Windows code page.
            self._proc = subprocess.Popen(
                [
                    "exiftool",
                    "-charset",
                    "filename=UTF8",
                    "-stay_open",
                    "True",
                    "-@",
                    "-",
                ],
                stdin=subprocess.PIPE,
                stdout=subprocess.PIPE,
                stderr=subprocess.DEVNULL,
                text=True,
                encoding="utf-8",
                errors="surrogateescape",
            )
        except FileNotFoundError as e:
            raise FileNotFoundError(
                "Exiftool is required but was not found. Please install it."
            ) from e

    def stop(self):
        if self._proc is None:
            return
        try:
            self._proc.stdin.write("-stay_open\nFalse\n")
            self._proc.stdin.flush()
            self._proc.wait(timeout=5)
        except Exception:
            try:
                self._proc.kill()
            except Exception:
                pass
        self._proc = None

    def _send_and_read(self, args):
        """Send args to the persistent process and read lines until sentinel.

        Returns list of stripped lines, or None on process death or on a
        UnicodeEncodeError that survived even after restarting the process
        with UTF-8 pipes.  Retries once if the process dies mid-write.
        """
        if self._proc is None:
            self.start()
        proc = self._proc
        payload = "\n".join(args) + "\n-execute\n"
        try:
            proc.stdin.write(payload)
            proc.stdin.flush()
        except (BrokenPipeError, OSError):
            self._proc = None
            self.start()
            proc = self._proc
            # Alan 5/4/26 - Catch BrokenPipeError/OSError on the retry too:
            # if the freshly-restarted ExifTool also dies during write/flush,
            # we previously tracebacked. Treat this like the encode failure:
            # warn, drop the process, return None so batch_query falls back
            # and the file is recorded with blank EXIF.
            try:
                proc.stdin.write(payload)
                proc.stdin.flush()
            except (BrokenPipeError, OSError, UnicodeEncodeError) as e:
                print(
                    f"Warning: ExifTool retry write failed: {e!r}; args={args!r}",
                    file=sys.stderr,
                )
                try:
                    if self._proc is not None:
                        self._proc.kill()
                except Exception:
                    pass
                self._proc = None
                return None
        except UnicodeEncodeError as e:
            # Should not happen with utf-8 pipes, but stay defensive: a single
            # bad path must not abort the whole scan.  Restart the process
            # (its stdin buffer state is now unknown) and let the caller fall
            # back to per-file querying.
            print(
                f"Warning: ExifTool stdin encode failed: {e!r}; args={args!r}",
                file=sys.stderr,
            )
            try:
                self._proc.kill()
            except Exception:
                pass
            self._proc = None
            return None

        lines = []
        while True:
            line = proc.stdout.readline()
            if not line:
                self._proc = None
                return None
            if line.strip().startswith("{ready"):
                break
            lines.append(line.strip())
        return lines

    def query(self, filepath):
        """Query a single file. Returns (date, gps_lat, gps_lon)."""
        args = ["-n", "-f", "-s", "-s", "-s", *self._TAGS, filepath]
        lines = self._send_and_read(args)
        if lines is None:
            return None, None, None
        return self._parse_lines(lines)

    def batch_query(self, filepaths, fast2=False):
        """Query multiple files at once using -json output.

        If fast2 is True, passes -fast2 to ExifTool, which skips MakerNotes
        and trailing metadata. Safe for JPEG/PNG/WebP. UNSAFE for RAW formats
        that store DateTimeOriginal in MakerNotes (e.g. older Nikon bodies).

        Returns dict mapping filepath -> (date, gps_lat, gps_lon).
        Files that fail to parse get (None, None, None).

        Matching between ExifTool's SourceFile and the requested paths uses
        exact string match or normalized abspath match only — never basename,
        because duplicate basenames across directories are common in photo trees.
        Unmatched results are silently dropped (conservative).
        """
        if not filepaths:
            return {}

        args = ["-json", "-n", "-f"]
        if fast2:
            args.append("-fast2")
        args.extend(self._TAGS)
        args.extend(filepaths)
        lines = self._send_and_read(args)

        retries = 3
        while lines is None and retries > 0:
            time.sleep(0.1)
            lines = self._send_and_read(args)
            retries -= 1

        results = {fp: (None, None, None) for fp in filepaths}
        if lines is None:
            # Fallback: query one at a time. query() intentionally full-parses;
            # this rare path prefers correctness over preserving fast2.
            for fp in filepaths:
                results[fp] = self.query(fp)
            return results

        # Parse the JSON array from the collected lines
        json_text = "\n".join(lines)
        try:
            records = json.loads(json_text)
        except (json.JSONDecodeError, ValueError):
            # Fallback: query one at a time. query() intentionally full-parses;
            # this rare path prefers correctness over preserving fast2.
            for fp in filepaths:
                results[fp] = self.query(fp)
            return results

        # Build lookup tables for safe matching (exact string, then abspath)
        exact_set = set(filepaths)
        abs_lookup = {}  # abspath -> original filepath
        for fp in filepaths:
            abs_lookup[os.path.abspath(fp)] = fp

        for rec in records:
            src = rec.get("SourceFile", "")
            if not src:
                continue

            # Safe match: exact string first, then normalized abspath
            if src in exact_set:
                matched_fp = src
            else:
                matched_fp = abs_lookup.get(os.path.abspath(src))
            if matched_fp is None:
                # Cannot safely identify which requested file this is — skip
                continue

            date = None
            for key in self._DATE_KEYS:
                val = rec.get(key)
                if val and isinstance(val, str) and val != "-":
                    if re.match(r"\d{4}:\d{2}:\d{2} \d{2}:\d{2}:\d{2}", val):
                        date = val
                        break

            gps_lat = None
            gps_lon = None
            lat_val = rec.get("GPSLatitude")
            lon_val = rec.get("GPSLongitude")
            if lat_val is not None and str(lat_val) != "-":
                gps_lat = str(lat_val)
            if lon_val is not None and str(lon_val) != "-":
                gps_lon = str(lon_val)

            results[matched_fp] = (date, gps_lat, gps_lon)

        return results

    def _parse_lines(self, lines):
        date = None
        if len(lines) >= self._DATE_TAG_COUNT:
            for i in range(self._DATE_TAG_COUNT):
                val = lines[i].strip()
                if val and val != "-":
                    if re.match(r"\d{4}:\d{2}:\d{2} \d{2}:\d{2}:\d{2}", val):
                        date = val
                        break
        gps_lat = None
        gps_lon = None
        if len(lines) >= self._DATE_TAG_COUNT + 2:
            lat = lines[self._DATE_TAG_COUNT].strip()
            lon = lines[self._DATE_TAG_COUNT + 1].strip()
            if lat and lat != "-":
                gps_lat = lat
            if lon and lon != "-":
                gps_lon = lon
        return date, gps_lat, gps_lon

    def __enter__(self):
        self.start()
        return self

    def __exit__(self, *exc):
        self.stop()


def get_photo_data(filepath, _exiftool=None):
    """Use exiftool to get the date and GPS data of a photo/video.

    If _exiftool (an ExifToolPersistent instance) is provided, uses the
    persistent process. Otherwise falls back to a one-shot subprocess call.
    """
    if _exiftool is not None:
        return _exiftool.query(filepath)

    # Legacy one-shot fallback
    try:
        date_tags = ["-DateTimeOriginal", "-CreateDate", "-MediaCreateDate"]
        tags = date_tags + ["-GPSLatitude", "-GPSLongitude"]

        result = subprocess.run(
            ["exiftool", "-n", "-f", *tags, "-s", "-s", "-s", filepath],
            stdout=subprocess.PIPE,
            stderr=subprocess.PIPE,
            text=True,
        )

        if result.returncode == 0:
            lines = result.stdout.splitlines()
            date = None
            if len(lines) >= len(date_tags):
                for i in range(len(date_tags)):
                    line = lines[i].strip()
                    if line and line != "-":
                        if re.match(r"\d{4}:\d{2}:\d{2} \d{2}:\d{2}:\d{2}", line):
                            date = line
                            break
            gps_lat = None
            gps_lon = None
            if len(lines) >= len(date_tags) + 2:
                gps_lat_str = lines[len(date_tags)].strip()
                gps_lon_str = lines[len(date_tags) + 1].strip()
                if gps_lat_str and gps_lat_str != "-":
                    gps_lat = gps_lat_str
                if gps_lon_str and gps_lon_str != "-":
                    gps_lon = gps_lon_str
            return date, gps_lat, gps_lon
        else:
            return None, None, None
    except FileNotFoundError as e:
        raise FileNotFoundError(
            "Exiftool is required but was not found. Please install it."
        ) from e


# Global variables for geolocation rate limiting and coordinate caching
_last_geolocate_time = 0
_coord_cache = {}  # (round(lat,2), round(lon,2)) -> location
_geolocate_lock = threading.Lock()


def _coord_cache_key(lat, lon, precision=LOCATION_CACHE_PRECISION):
    try:
        return (round(float(lat), precision), round(float(lon), precision))
    except (ValueError, TypeError):
        return None


def geolocate(lat, lon, location_conn=None, pending_counter=None):
    """Convert GPS coordinates to a human-readable location using Nominatim API."""
    global _last_geolocate_time

    if not lat or not lon:
        return None

    try:
        # Convert to float to ensure valid coordinates
        lat_float = float(lat)
        lon_float = float(lon)
    except (ValueError, TypeError):
        return f"{lat}, {lon}"

    # Check coordinate cache (round to 2 decimal places ~1 km precision)
    cache_key = _coord_cache_key(lat_float, lon_float)
    if cache_key is None:
        return f"{lat}, {lon}"
    if cache_key in _coord_cache:
        return _coord_cache[cache_key]

    lat_rounded, lon_rounded = cache_key
    cached_location = get_cached_location(
        location_conn,
        lat_rounded,
        lon_rounded,
        LOCATION_CACHE_PRECISION,
    )
    if cached_location:
        with _geolocate_lock:
            _coord_cache[cache_key] = cached_location
        return cached_location

    with _geolocate_lock:
        # Rate limiting: wait at least 1 second since last request
        current_time = time.time()
        time_since_last = current_time - _last_geolocate_time
        if time_since_last < 1.0:
            time.sleep(1.0 - time_since_last)

        try:
            # Build Nominatim reverse geocoding URL with addressdetails=1
            url = f"https://nominatim.openstreetmap.org/reverse?format=json&lat={lat_float}&lon={lon_float}&addressdetails=1"

            # Create request with proper User-Agent header (required by Nominatim)
            req = urllib.request.Request(url)
            req.add_header(
                "User-Agent",
                "findphotodates.py/1.5 (https://github.com/alanrockefeller/findphotodates.py)",
            )

            # Make the request
            with urllib.request.urlopen(req, timeout=10) as response:
                data = json.loads(response.read().decode())

                # Update last request time
                _last_geolocate_time = time.time()

                # Extract location from response - prefer address components over display_name
                location = None
                if "address" in data:
                    addr = data["address"]
                    parts = []
                    # Build location from address components in order of specificity
                    for key in [
                        "city",
                        "town",
                        "village",
                        "county",
                        "state",
                        "country",
                    ]:
                        if key in addr:
                            parts.append(addr[key])
                    if parts:
                        location = ", ".join(parts)

                # Fallback to display_name if address components didn't work
                if not location and "display_name" in data:
                    location = data["display_name"]

                # Fallback to coordinates if no location found
                if not location:
                    location = f"{lat_float}, {lon_float}"

        except urllib.error.HTTPError as e:
            # Handle 429 (rate limit) with longer backoff
            if e.code == 429:
                _last_geolocate_time = (
                    time.time() + 5
                )  # Wait 5 seconds before next attempt
            else:
                _last_geolocate_time = time.time()
            # Don't cache fallback coordinates - allow retry on next run
            return f"{lat_float}, {lon_float}"
        except Exception:
            # Don't cache fallback coordinates - allow retry on next run
            _last_geolocate_time = time.time()
            return f"{lat_float}, {lon_float}"

    with _geolocate_lock:
        _coord_cache[cache_key] = location
    put_cached_location(
        location_conn,
        lat_rounded,
        lon_rounded,
        LOCATION_CACHE_PRECISION,
        location,
        pending_counter=pending_counter,
    )
    return location


def is_coordinate_string(location):
    """Check if location string looks like raw coordinates (e.g., "37.7, -122.4")."""
    if not location:
        return False
    # Match pattern: optional negative, digits, optional decimal, comma, optional space, optional negative, digits, optional decimal
    return bool(re.match(r"^-?\d+(\.\d+)?,\s*-?\d+(\.\d+)?$", location.strip()))


def load_cache(output_file, debug=False):
    """Load existing TSV cache if it exists and has the expected header.

    Accepts both current columns (with content_hash) and legacy 7-column TSV.
    Skips all leading # lines, then uses the first non-# line as the header row.
    """
    cache = {}
    if not os.path.exists(output_file):
        return cache

    try:
        with open(output_file, "r", encoding="utf-8", newline="") as f:
            # Consume all leading # lines
            first_non_comment = None
            while True:
                line = f.readline()
                if not line:
                    break
                line = line.rstrip("\n\r")
                if not line:
                    continue
                if line.startswith("#"):
                    continue

                # First non-# line
                first_non_comment = line
                break

            if not first_non_comment:
                return cache

            # Parse column header with csv so quoted tabs are handled
            fieldnames = next(csv.reader([first_non_comment], delimiter="\t"))
            if not fieldnames:
                return cache

            # Sanity check: require at least 'filepath' and 'date_taken'
            if "filepath" not in fieldnames or "date_taken" not in fieldnames:
                if debug:
                    print(
                        f"Debug: Cache file {output_file} missing required columns. Found: {fieldnames}"
                    )
                return cache

            expected = list(TSV_COLUMNS)
            legacy = expected[:-1]
            if fieldnames != expected and fieldnames != legacy:
                if debug:
                    print(
                        f"Debug: Cache file {output_file} header mismatch. Found: {fieldnames}"
                    )
                return cache

            for row in csv.DictReader(
                f, fieldnames=fieldnames, delimiter="\t", restval=""
            ):
                filepath = row.get("filepath", "")
                if filepath:
                    try:
                        # Normalize for cross-platform cache hits (WSL ↔ Windows).
                        abs_filepath = _normalize_cache_key(filepath)
                        size_bytes = int(row.get("size_bytes", "0"))
                        mtime_ns = int(row.get("mtime_ns", "0"))
                        date_taken = row.get("date_taken", "") or None
                        gps_lat = row.get("gps_lat", "") or None
                        gps_lon = row.get("gps_lon", "") or None
                        location = row.get("location", "") or None

                        if (
                            gps_lat
                            and gps_lon
                            and location
                            and not is_coordinate_string(location)
                        ):
                            coord_key = _coord_cache_key(gps_lat, gps_lon)
                            if coord_key is not None:
                                with _geolocate_lock:
                                    _coord_cache.setdefault(coord_key, location)

                        # Treat coordinate strings as missing location (allows retry)
                        if location and is_coordinate_string(location):
                            location = None

                        cache[abs_filepath] = {
                            "size_bytes": size_bytes,
                            "mtime_ns": mtime_ns,
                            "date_taken": date_taken,
                            "gps_lat": gps_lat,
                            "gps_lon": gps_lon,
                            "location": location,
                        }
                    except (ValueError, KeyError):
                        continue
    except (IOError, UnicodeDecodeError) as e:
        if debug:
            print(f"Debug: Error reading cache file {output_file}: {e}")
        return cache

    return cache


def write_dates_to_file_atomic(
    output_file,
    photo_data,
    inventory_root=None,
    hash_options=None,
    old_format=False,
    path_style="linux",  # safe internal default; CLI default is "auto" (resolved before calling)
):
    """Write photo data to file atomically.

    If old_format is True, writes legacy line-based format: ./relative/path: YYYY:MM:DD HH:MM:SS
    (no hashes). Otherwise writes NEW TSV format with optional header comments and content_hash column.
    photo_data: list of 8-tuples (filepath, date_taken, size_bytes, mtime_ns, gps_lat, gps_lon, location, content_hash)
    for NEW format; for OLD format the 8th element is ignored.
    """
    output_dir = os.path.dirname(os.path.abspath(output_file))
    if not output_dir:
        output_dir = "."

    # Compute the display-formatted inventory root once.  os.path.abspath()
    # must be called *before* format_path_style() — calling it after would
    # mangle cross-platform display paths (e.g. os.path.abspath("C:\\Photos")
    # on Linux produces a bogus POSIX path).
    display_root = None
    local_root = None
    if inventory_root is not None:
        local_root = os.path.abspath(inventory_root)
        display_root = format_path_style(local_root, path_style)

    formatted_data = []
    for row in photo_data:
        new_row = list(row)
        new_row[0] = format_path_style(row[0], path_style)
        formatted_data.append(tuple(new_row))
    photo_data = formatted_data

    suffix = ".txt" if old_format else ".tsv"
    fd, temp_file = tempfile.mkstemp(
        dir=output_dir, suffix=suffix, prefix=".findphotodates_"
    )
    try:
        with os.fdopen(fd, "w", encoding="utf-8", newline="") as f:
            sorted_data = sorted(photo_data, key=lambda r: r[0])
            if old_format:
                # OLD format: ./relative/path: YYYY:MM:DD HH:MM:SS (no hashes)
                # Use the display-formatted root with a style-appropriate
                # separator so that prefix stripping works correctly even
                # when the display style differs from the host OS.
                if display_root:
                    sep = "\\" if path_style == "windows" else "/"
                    root = display_root + sep
                else:
                    root = ""
                for row in sorted_data:
                    filepath = row[0]
                    date_taken = (row[1] or "").strip()
                    if not date_taken:
                        continue
                    if root and filepath.startswith(root):
                        rel = "./" + filepath[len(root) :].replace("\\", "/")
                    else:
                        rel = filepath
                    f.write(f"{rel}: {date_taken}\n")
            else:
                # NEW format: comment block, then TSV with content_hash column
                if display_root is not None:
                    f.write(f"# inventory_root={display_root}\n")
                if hash_options is not None:
                    f.write(f"# hash_mode={hash_options.hash_mode}\n")
                    if hash_options.hash_mode == "sample":
                        f.write("# content_hash_format=samplehash_v1\n")
                        f.write(
                            f"# samplehash_v1 algo={hash_options.sample_algo} chunks={hash_options.sample_chunks} chunk_mib={hash_options.sample_chunk_mib:g}\n"
                        )
                    elif hash_options.hash_mode == "full":
                        # full_algo is blake3 or sha256 (no blake2b for full)
                        f.write("# content_hash_format=fullhash_v1\n")
                        f.write(f"# fullhash algo={hash_options.full_algo}\n")
                f.write(TSV_HEADER + "\n")
                writer = csv.writer(f, delimiter="\t", quoting=csv.QUOTE_MINIMAL)
                writer.writerow(TSV_COLUMNS)
                for row in sorted_data:
                    filepath = row[0]
                    date_taken = row[1] if len(row) > 1 else ""
                    size_bytes = row[2] if len(row) > 2 else 0
                    mtime_ns = row[3] if len(row) > 3 else 0
                    gps_lat = row[4] if len(row) > 4 else ""
                    gps_lon = row[5] if len(row) > 5 else ""
                    location = row[6] if len(row) > 6 else ""
                    content_hash = row[7] if len(row) > 7 else ""
                    writer.writerow(
                        [
                            filepath,
                            date_taken or "",
                            str(size_bytes),
                            str(mtime_ns),
                            gps_lat or "",
                            gps_lon or "",
                            location or "",
                            content_hash or "",
                        ]
                    )
        os.replace(temp_file, output_file)
    except Exception:
        try:
            os.unlink(temp_file)
        except Exception:
            pass
        raise


def summarize_results(photo_data, file_counts, quiet, debug=False):
    """Print a summary of results."""
    if quiet:
        return

    print("\nSummary of Results:\n")

    total_files = len(photo_data)
    dated_files = sum(1 for e in photo_data if e[1])
    print(f"Files indexed: {total_files:,} ({dated_files:,} with EXIF dates)")

    # Print counts by file type (top 15 by count, rest grouped as "Other")
    # (no extension) is always shown separately if present.
    print("\nFiles by extension:")
    no_ext_count = file_counts.pop("", None)
    sorted_exts = sorted(file_counts.items(), key=lambda x: (-x[1], x[0]))
    top = sorted_exts[:15]
    rest = sorted_exts[15:]
    for ext, count in top:
        print(f"  {ext.upper()}: {count:,}")
    if rest:
        other_count = sum(c for _, c in rest)
        print(f"  Other ({len(rest)} types): {other_count:,}")
    if no_ext_count is not None:
        print(f"  (no extension): {no_ext_count:,}")
        file_counts[""] = no_ext_count  # restore for downstream use

    # Calculate date summaries
    date_counter = Counter()
    for entry in photo_data:
        # Entry is a tuple: (filepath, date_taken, size_bytes, mtime_ns, gps_lat, gps_lon, location, content_hash)
        date_str = entry[1] if len(entry) > 1 else None

        # Skip None or empty dates
        if not date_str:
            continue

        try:
            # Parse exiftool date format: YYYY:MM:DD HH:MM:SS
            dt = datetime.strptime(date_str, "%Y:%m:%d %H:%M:%S")
            day_key = dt.strftime("%Y-%m-%d")
            date_counter[day_key] += 1
        except (ValueError, AttributeError, TypeError) as e:
            # Gracefully skip invalid dates
            if debug:
                print(f"Warning: Could not parse date '{date_str}': {str(e)}")
            continue

    # Determine file type label for summary
    media_exts = MEDIA_EXTENSIONS
    valid_exts = [ext for ext in file_counts if ext]
    if valid_exts and all(ext in VIDEO_EXTENSIONS for ext in valid_exts):
        file_type_label = "Videos"
    elif valid_exts and all(ext in PHOTO_EXTENSIONS for ext in valid_exts):
        file_type_label = "Photos"
    elif valid_exts and all(ext in media_exts for ext in valid_exts):
        file_type_label = "Media"
    else:
        file_type_label = "Files with EXIF dates"

    if len(date_counter) <= 20:
        print(f"\n{file_type_label} Taken per day:")
        for day, count in sorted(date_counter.items()):
            print(f"  {day}: {count}")
    else:
        month_counter = defaultdict(int)
        for day_key in date_counter:
            try:
                # Parse the day_key (YYYY-MM-DD) to get year-month
                dt = datetime.strptime(day_key, "%Y-%m-%d")
                year_month_key = dt.strftime("%Y-%m")
                month_counter[year_month_key] += date_counter[day_key]
            except (ValueError, AttributeError) as e:
                # Gracefully skip invalid day keys
                if debug:
                    print(
                        f"Warning: Could not extract year-month from '{day_key}': {str(e)}"
                    )
                continue
        print(f"\n{file_type_label} Taken per Month:")
        for year_month, count in sorted(month_counter.items()):
            print(f"  {year_month}: {count}")


