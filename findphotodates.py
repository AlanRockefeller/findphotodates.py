    test_photo_data = [
        (
            "/path/to/file1.jpg",
            "2025:12:31 23:59:59",
            1000,
            1234567890,
            "37.7749",
            "-122.4194",
            None,
            "hash1",
        ),
        (
            "/path/to/file2.jpg",
            "2025:12:31 12:00:00",
            2000,
            1234567891,
            "34.0522",
            "-118.2437",
            None,
            "hash2",
        ),
        (
            "/path/to/file3.jpg",
            "2025:12:30 10:30:00",
            3000,
            1234567892,
            None,
            None,
            None,
            "hash3",
        ),
    ]
    date_counter = Counter()
    for entry in test_photo_data:
        date_str = entry[1]
        if date_str:
            dt = datetime.strptime(date_str, "%Y:%m:%d %H:%M:%S")
            date_counter[dt.strftime("%Y-%m-%d")] += 1
    assert date_counter["2025-12-31"] == 2
    assert date_counter["2025-12-30"] == 1
    print("✓ All date parsing tests passed!")


