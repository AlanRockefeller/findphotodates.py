def run_scan(
    directory,
    output,
    extensions,
    locate=False,
    quiet=False,
    debug=False,
    hash_options=None,
    location_options=None,
    old_format=False,
    perf_stats=None,
    path_style="linux",  # safe internal default; CLI default is "auto" (resolved before calling)
    raw_output=None,  # Alan 5/3/26 - original CLI --output arg, used only for friendly Windows path hints
    workers=4,
    min_image_size=MIN_EXIFTOOL_IMAGE_BYTES,
):
    """Run a scan on the specified directory.

    If perf_stats is a dict, it will be populated with detailed timing breakdowns.
    """
    _t0 = time.time()
    worker_count = _clamp_worker_count(workers)
    min_image_size = _clamp_min_image_size(min_image_size)

    # Resolve "auto" path style once, before any writes.
    if path_style == "auto":
        path_style = resolve_output_path_style("auto", output, directory)

    # Alan 5/3/26 - Validate input + output paths up front. This previously
    # only checked that the input directory existed, and the output path was
    # not validated at all — a bad --output dir would survive a multi-minute
    # scan and then crash at the first checkpoint when tempfile.mkstemp()
    # tried to create the temp file in a non-existent parent directory.
    if not validate_scan_paths(directory, output, raw_output=raw_output):
        return False

    if hash_options is None:
        hash_options = parse_hash_args(quiet=quiet)
    if location_options is None:
        location_options = parse_location_args()

    if old_format:
        # Force hashing off for old format
        hash_options.hash_mode = "off"

    hash_conn = (
        open_sqlite_cache(hash_options.hash_cache_path, debug=debug)
        if (hash_options.use_hash_cache and hash_options.hash_mode != "off")
        else None
    )
    hash_cache_pending = [0] if hash_conn else None
    location_conn = (
        open_location_cache(location_options.location_cache_path, debug=debug)
        if (locate and location_options.use_location_cache)
        else None
    )
    location_cache_pending = [0] if location_conn else None

    if not quiet and hash_options.hash_mode != "off":
        msg = f"Hashing enabled ({hash_options.hash_mode})"
        if hash_options.hash_mode == "sample":
            msg += f": {hash_options.sample_chunks}x{hash_options.sample_chunk_mib:g} MiB {hash_options.sample_algo}"
        else:
            msg += f": {hash_options.full_algo}"
        msg += ". First run may be slower; results cached in SQLite."
        print(msg)

    inventory_root = os.path.abspath(directory)
    # Alan 5/3/26 - Existing path check moved into validate_scan_paths() above.

    if not quiet:
        if extensions is None:
            print(f"Indexing all files in '{directory}'...")
        else:
            print(
                f"Searching for files in '{directory}' with extensions: {', '.join(sorted(extensions))}..."
            )

    if not quiet and old_format and output.lower().endswith(".tsv"):
        print(
            "Warning: Using .tsv extension for legacy line-based format. Recommended: .txt"
        )

    # Load existing cache
    _t_cache_start = time.time()
    cache = load_cache(output, debug=debug)
    _t_cache_load = time.time() - _t_cache_start
    if not quiet and cache:
        print(f"Loaded {len(cache)} entries from cache.")

    # Compute the absolute and realpath forms of the output file so we can
    # skip it during discovery (avoids self-indexing when the inventory file
    # lives inside the directory being scanned).
    _output_abs = os.path.abspath(output)
    _output_real = os.path.realpath(output)

    # Redirect warnings to a unique log file per run
    error_log_path = os.path.join(
        tempfile.gettempdir(),
        f"findphotodates.error.{os.getpid()}.{int(time.time())}.log",
    )
    photo_data = []
    file_counts = Counter()
    cached_count = 0
    processed_count = 0
    # Detailed timing accumulators
    _t_realpath_total = 0.0
    _t_stat_total = 0.0
    _t_cache_lookup_total = 0.0
    _t_exiftool_total = 0.0
    _t_hashing_total = 0.0
    _t_geolocate_total = 0.0
    _t_checkpoint_total = 0.0
    _t_dispatch_wait = 0.0
    _t_results_wait = 0.0
    _t_worker_total = 0.0
    _worker_details = []

    def cleanup_all():
        if hash_conn is not None:
            try:
                with _sqlite_lock:
                    if hash_cache_pending is not None and hash_cache_pending[0] > 0:
                        hash_conn.commit()
                hash_conn.close()
            except Exception:
                pass
        if location_conn is not None:
            try:
                with _sqlite_lock:
                    if (
                        location_cache_pending is not None
                        and location_cache_pending[0] > 0
                    ):
                        location_conn.commit()
                location_conn.close()
            except Exception:
                pass

    # --- Start background file discovery ---
    file_queue = queue.Queue(maxsize=_DISCOVERY_QUEUE_SIZE)
    discovery = _DiscoveryState()
    producer = threading.Thread(
        target=_discovery_producer,
        args=(file_queue, directory, extensions, discovery, debug),
        daemon=True,
    )
    producer.start()
    _t_discovery_start = time.time()

    interrupted = False

    with open(error_log_path, "w", encoding="utf-8") as error_log:
        start_time = time.time()
        last_save_time = start_time
        last_progress_time = start_time
        rate_tracker = _RateTracker(window_seconds=30.0)
        discovery_complete = (
            False  # local flag: have we printed the discovery-done message?
        )
        total_files = 0  # set once discovery finishes

        work_queue = queue.Queue(maxsize=max(1, worker_count * 4))
        results_queue = queue.Queue()
        stop_event = threading.Event()
        error_log_lock = threading.Lock()
        worker_threads = []
        worker_done_count = 0

        for worker_id in range(1, worker_count + 1):
            t = threading.Thread(
                target=_exiftool_worker,
                args=(
                    worker_id,
                    work_queue,
                    results_queue,
                    stop_event,
                    locate,
                    hash_options,
                    hash_conn,
                    hash_cache_pending,
                    location_conn,
                    location_cache_pending,
                    error_log,
                    error_log_lock,
                ),
                daemon=True,
            )
            t.start()
            worker_threads.append(t)

        # Batch buffer for files that need exiftool queries
        # Each entry: (out_path, os_path, key_path, size_bytes, mtime_ns, ext)
        #   out_path  — abspath written to TSV (human-visible)
        #   os_path   — actual filesystem path for exiftool / hashing I/O
        #   key_path  — normalized cache key for TSV cache and hash cache lookups
        exiftool_batch = []

        def _drain_results():
            nonlocal processed_count, _t_exiftool_total, _t_hashing_total
            nonlocal _t_geolocate_total, _t_results_wait, _t_worker_total
            nonlocal worker_done_count
            _trq0 = time.time()
            while True:
                try:
                    packet = results_queue.get_nowait()
                except queue.Empty:
                    break
                if packet.get("type") == "results":
                    rows = packet.get("results", [])
                    photo_data.extend(rows)
                    file_counts.update(packet.get("file_counts", Counter()))
                    processed_count += len(rows)
                    _t_exiftool_total += packet.get("t_exiftool", 0.0)
                    _t_hashing_total += packet.get("t_hashing", 0.0)
                    _t_geolocate_total += packet.get("t_geolocate", 0.0)
                elif packet.get("type") == "worker_done":
                    stats = packet.get("stats", {})
                    _worker_details.append(stats)
                    _t_worker_total += stats.get("wall", 0.0)
                    worker_done_count += 1
            _t_results_wait += time.time() - _trq0

        def _update_progress_and_checkpoint():
            nonlocal last_progress_time, last_save_time, _t_checkpoint_total
            total_seen = cached_count + processed_count
            current_time = time.time()
            rate_tracker.record(current_time, total_seen)

            if total_seen > 0 and (current_time - last_save_time) > 900:
                _tc0 = time.time()
                data_snapshot = list(photo_data)
                _ckpt_ok = _safe_write_inventory(
                    output,
                    data_snapshot,
                    inventory_root,
                    hash_options,
                    old_format,
                    path_style,
                    debug=debug,
                    context="checkpoint",
                )
                _t_checkpoint_total += time.time() - _tc0
                last_save_time = current_time
                if not quiet:
                    _clear_progress_line()
                    if _ckpt_ok:
                        print(f"[Checkpoint: {len(data_snapshot):,} files saved]")
                if not _ckpt_ok:
                    print(
                        "Error: Checkpoint failed; stopping scan so you can "
                        "fix the output path/disk/permissions and rerun."
                    )
                    stop_event.set()
                    return False

            if not quiet and (current_time - last_progress_time) >= 2.0:
                elapsed = current_time - start_time
                rate = rate_tracker.rate()
                rate_str = f"{rate:.0f} files/sec" if rate else "..."

                if discovery_complete and total_files > 0:
                    pct = total_seen / total_files * 100
                    eta_str = _format_eta(total_files - total_seen, rate)
                    eta_part = f" — ETA: {eta_str}" if eta_str else ""
                    print(
                        f"Processed {total_seen:,} / {total_files:,} ({pct:.1f}%) — "
                        f"{rate_str}{eta_part} [{_format_duration(elapsed)}]",
                        end="\r" if _IS_TTY else "\n",
                    )
                else:
                    print(
                        f"Discovering... {discovery.discovered:,} found | "
                        f"Processed {total_seen:,} ({cached_count:,} cached) — "
                        f"{rate_str} [{_format_duration(elapsed)}]",
                        end="\r" if _IS_TTY else "\n",
                    )
                last_progress_time = current_time
            return True

        def _maybe_print_discovery_complete():
            nonlocal discovery_complete, total_files
            if not discovery_complete and discovery.done.is_set():
                discovery_complete = True
                total_files = discovery.total
                if not quiet:
                    _clear_progress_line()
                    print(f"Discovery complete: {total_files:,} files found.")

        def _record_blank_exif_batch(batch):
            nonlocal processed_count, _t_hashing_total
            blank_results = {item[1]: (None, None, None) for item in batch}
            rows, counts, stats = _build_exiftool_rows(
                batch,
                blank_results,
                locate,
                location_conn,
                location_cache_pending,
                hash_options,
                hash_conn,
                hash_cache_pending,
            )
            photo_data.extend(rows)
            file_counts.update(counts)
            processed_count += len(rows)
            _t_hashing_total += stats["t_hashing"]

        def _index_as_non_media(out_path, fs_path, key_path, size_bytes, mtime_ns, ext):
            nonlocal processed_count, _t_hashing_total
            _th0 = time.time()
            content_hash = get_content_hash(
                key_path,
                size_bytes,
                mtime_ns,
                hash_options,
                hash_conn,
                hash_cache_pending,
                os_path=fs_path,
            )
            _t_hashing_total += time.time() - _th0
            photo_data.append(
                (
                    out_path,
                    "",
                    size_bytes,
                    mtime_ns,
                    "",
                    "",
                    "",
                    content_hash,
                )
            )
            processed_count += 1
            file_counts[ext] += 1

        def _enqueue_exiftool_batch():
            if not exiftool_batch:
                return True
            batch = list(exiftool_batch)
            exiftool_batch.clear()
            while True:
                try:
                    work_queue.put(batch, timeout=0.5)
                    return True
                except queue.Full:
                    _drain_results()
                    _maybe_print_discovery_complete()
                    if not _update_progress_and_checkpoint():
                        return False
                    if not any(t.is_alive() for t in worker_threads):
                        _log_worker_error(
                            error_log,
                            error_log_lock,
                            f"Warning: all ExifTool workers stopped; recording {len(batch)} file(s) with blank EXIF.",
                        )
                        _record_blank_exif_batch(batch)
                        return True

        def _stop_workers(interrupt=False):
            if interrupt:
                stop_event.set()
                for _ in worker_threads:
                    try:
                        work_queue.put_nowait(None)
                    except queue.Full:
                        pass
            else:
                for _ in worker_threads:
                    work_queue.put(None)
            timeout_seconds = 5 if interrupt else WORKER_SHUTDOWN_TIMEOUT_SECONDS
            deadline = time.time() + timeout_seconds
            while any(t.is_alive() for t in worker_threads):
                for t in worker_threads:
                    remaining = max(0.0, deadline - time.time())
                    if remaining <= 0:
                        alive = sum(1 for thread in worker_threads if thread.is_alive())
                        if alive:
                            print(
                                f"Warning: {alive} ExifTool worker(s) did not exit within {timeout_seconds} seconds; abandoning.",
                                file=sys.stderr,
                            )
                        return False
                    t.join(timeout=min(0.2, remaining))
                _drain_results()
                if not interrupt:
                    _update_progress_and_checkpoint()
            _drain_results()
            return True

        try:
            while True:
                _drain_results()
                _maybe_print_discovery_complete()
                if not _update_progress_and_checkpoint():
                    _stop_workers(interrupt=True)
                    cleanup_all()
                    return False

                try:
                    _td0 = time.time()
                    item = file_queue.get(timeout=0.5)
                    _t_dispatch_wait += time.time() - _td0
                except queue.Empty:
                    _t_dispatch_wait += time.time() - _td0
                    if discovery.done.is_set() and file_queue.empty():
                        if discovery.error is not None:
                            print(f"\nError during file discovery: {discovery.error}")
                            raise discovery.error from None
                        break
                    continue

                if item is None:
                    if discovery.error is not None:
                        if not quiet:
                            print(f"\nError during file discovery: {discovery.error}")
                        raise discovery.error
                    break

                file, is_symlink, prod_size, prod_mtime = item
                basename = os.path.basename(file)
                ext = os.path.splitext(basename)[1].lstrip(".").lower()

                try:
                    out_path = os.path.abspath(file)
                    # Skip the inventory file itself to avoid self-indexing
                    if out_path == _output_abs or out_path == _output_real:
                        continue
                    # key_path is always derived from out_path (what's stored in
                    # the TSV) so that cache lookups match what load_cache keys on.
                    key_path = _normalize_cache_key(out_path)
                    if is_symlink:
                        # Symlink: resolve target for I/O (stat, exiftool, hashing)
                        _tr0 = time.time()
                        fs_path = os.path.realpath(out_path)
                        _t_realpath_total += time.time() - _tr0
                        # Skip if symlink target is the inventory file
                        if fs_path == _output_real:
                            continue
                        _ts0 = time.time()
                        stat_info = os.stat(fs_path)
                        size_bytes = stat_info.st_size
                        mtime_ns = stat_info.st_mtime_ns
                        _t_stat_total += time.time() - _ts0
                    else:
                        # Non-symlink: reuse producer-side metadata (no stat syscall)
                        fs_path = out_path
                        size_bytes = prod_size
                        mtime_ns = prod_mtime

                    # Check TSV cache
                    _tcl0 = time.time()
                    cached_entry = cache.get(key_path)
                    _t_cache_lookup_total += time.time() - _tcl0

                    if (
                        cached_entry
                        and cached_entry["size_bytes"] == size_bytes
                        and cached_entry["mtime_ns"] == mtime_ns
                    ):
                        date_taken = cached_entry["date_taken"]
                        gps_lat = cached_entry.get("gps_lat")
                        gps_lon = cached_entry.get("gps_lon")
                        location = cached_entry.get("location")

                        if locate and gps_lat and gps_lon:
                            if not location or is_coordinate_string(location):
                                _tg0 = time.time()
                                location = geolocate(
                                    gps_lat,
                                    gps_lon,
                                    location_conn=location_conn,
                                    pending_counter=location_cache_pending,
                                )
                                _t_geolocate_total += time.time() - _tg0
                                cached_entry["location"] = location

                        _th0 = time.time()
                        content_hash = get_content_hash(
                            key_path,
                            size_bytes,
                            mtime_ns,
                            hash_options,
                            hash_conn,
                            hash_cache_pending,
                            os_path=fs_path,
                        )
                        _t_hashing_total += time.time() - _th0
                        photo_data.append(
                            (
                                out_path,
                                date_taken or "",
                                size_bytes,
                                mtime_ns,
                                gps_lat or "",
                                gps_lon or "",
                                location or "",
                                content_hash,
                            )
                        )
                        cached_count += 1
                        # fall through to progress/checkpoint below

                    else:
                        # Not cached — check if exiftool-eligible
                        if ext in EXIFTOOL_EXTENSIONS:
                            # Tiny images (icons, thumbnails) almost never have EXIF.
                            # RAW/video/other EXIFTOOL_EXTENSIONS are not filtered.
                            skip_for_size = (
                                min_image_size > 0
                                and ext in SIZE_FILTERED_IMAGE_EXTENSIONS
                                and size_bytes < min_image_size
                            )
                            if skip_for_size:
                                _index_as_non_media(
                                    out_path,
                                    fs_path,
                                    key_path,
                                    size_bytes,
                                    mtime_ns,
                                    ext,
                                )
                            else:
                                exiftool_batch.append(
                                    (
                                        out_path,
                                        fs_path,
                                        key_path,
                                        size_bytes,
                                        mtime_ns,
                                        ext,
                                    )
                                )
                                if len(exiftool_batch) >= EXIFTOOL_BATCH_SIZE:
                                    if not _enqueue_exiftool_batch():
                                        _stop_workers(interrupt=True)
                                        cleanup_all()
                                        return False
                        else:
                            # Non-media file: index with filesystem metadata only
                            _index_as_non_media(
                                out_path,
                                fs_path,
                                key_path,
                                size_bytes,
                                mtime_ns,
                                ext,
                            )
                    if (
                        cached_entry
                        and cached_entry["size_bytes"] == size_bytes
                        and cached_entry["mtime_ns"] == mtime_ns
                    ):
                        file_counts[ext] += 1

                except OSError as e:
                    error_log.write(f"Warning: Could not access '{file}': {str(e)}\n")
                    if any(
                        err in str(e).lower()
                        for err in [
                            "no such file",
                            "input/output error",
                            "stale file handle",
                        ]
                    ):
                        _stop_workers(interrupt=True)
                        _drain_results()
                        if _check_drive_disconnect(
                            e,
                            output,
                            photo_data,
                            inventory_root,
                            hash_options,
                            old_format,
                            cleanup_all,
                            path_style=path_style,
                        ):
                            return False
                    continue

            if not _enqueue_exiftool_batch():
                _stop_workers(interrupt=True)
                cleanup_all()
                return False
            workers_finished = _stop_workers(interrupt=False)
            if workers_finished:
                done_deadline = time.time() + 10
                while worker_done_count < worker_count:
                    _drain_results()
                    if worker_done_count >= worker_count:
                        break
                    if time.time() >= done_deadline:
                        missing = worker_count - worker_done_count
                        print(
                            f"Warning: missing {missing} ExifTool worker completion packet(s); continuing.",
                            file=sys.stderr,
                        )
                        break
                    time.sleep(0.05)

        except Exception:
            _stop_workers(interrupt=True)
            _drain_results()
            raise
        except KeyboardInterrupt:
            interrupted = True
            _stop_workers(interrupt=True)
            _drain_results()
            print(
                f"\n\nInterrupted! Saving progress ({len(photo_data):,} files processed so far)..."
            )
            # Alan 5/3/26 - _safe_write_inventory already prints a friendly
            # error block on predictable failures; only show a generic
            # warning for unexpected exceptions.
            try:
                if _safe_write_inventory(
                    output,
                    list(photo_data),
                    inventory_root,
                    hash_options,
                    old_format,
                    path_style,
                    debug=debug,
                    context="interrupted save",
                ):
                    print(f"Progress saved to '{output}'.")
            except Exception as save_err:
                print(f"WARNING: Could not save progress: {save_err}")
            print("\nTo resume, run the same command again:")
            print(f'  findphotodates.py --directory "{directory}" -o "{output}"')
            print(
                "The scan will pick up where it left off using the saved inventory as cache."
            )

    # Wait for producer thread to finish (should already be done)
    producer.join(timeout=5)

    if interrupted:
        cleanup_all()
        return False

    total_seen = cached_count + processed_count
    if total_seen == 0:
        if not quiet:
            print("No files found.")
        cleanup_all()
        return True

    if not quiet:
        elapsed = time.time() - start_time
        # Clear \r progress line, then print clean completion summary
        _clear_progress_line()
        print(
            f"Processing complete: {total_seen:,} files in {_format_duration(elapsed)}."
        )

    _tw0 = time.time()
    # Alan 5/3/26 - Wrap final write in _safe_write_inventory so the user
    # gets a friendly error (not a Python traceback) if the output dir
    # disappeared, filled up, or revoked write access during the scan.
    _final_ok = _safe_write_inventory(
        output,
        photo_data,
        inventory_root,
        hash_options,
        old_format,
        path_style,
        debug=debug,
        context="final write",
    )
    _t_write = time.time() - _tw0

    if not _final_ok:
        cleanup_all()
        return False

    if not quiet:
        print(f"Dates written to '{output}' ({len(photo_data):,} files listed).")
        if cached_count > 0:
            print(
                f"  ({cached_count:,} from cache, {processed_count:,} newly processed)"
            )

    summarize_results(photo_data, file_counts, quiet, debug)
    cleanup_all()

    if perf_stats is not None:
        _t_total = time.time() - _t0
        # Discovery wall time = producer thread lifetime
        _t_discovery_wall = (
            discovery.done_time - _t_discovery_start if discovery.done_time else 0.0
        )
        perf_stats["wall_total"] = _t_total
        perf_stats["t_cache_load"] = _t_cache_load
        perf_stats["t_discovery_wall"] = _t_discovery_wall
        perf_stats["t_discovery_walk"] = discovery.t_walk
        perf_stats["t_discovery_queue_wait"] = discovery.t_queue_wait
        perf_stats["t_realpath"] = _t_realpath_total
        perf_stats["t_stat"] = _t_stat_total
        perf_stats["t_cache_lookup"] = _t_cache_lookup_total
        perf_stats["t_exiftool"] = _t_exiftool_total
        perf_stats["t_hashing"] = _t_hashing_total
        perf_stats["t_geolocate"] = _t_geolocate_total
        perf_stats["t_checkpoints"] = _t_checkpoint_total
        perf_stats["t_dispatch_wait"] = _t_dispatch_wait
        perf_stats["t_results_wait"] = _t_results_wait
        perf_stats["t_worker_total"] = _t_worker_total
        perf_stats["worker_details"] = sorted(
            _worker_details, key=lambda item: item.get("worker_id", 0)
        )
        perf_stats["worker_count"] = worker_count
        perf_stats["t_write"] = _t_write
        perf_stats["files_total"] = total_seen
        perf_stats["files_cached"] = cached_count
        perf_stats["files_processed"] = processed_count

    return True


def _print_perf_summary(label, stats):
    """Print a performance timing breakdown for a single scan."""
    wall = stats.get("wall_total", 0)
    if wall <= 0:
        return
    worker_count = stats.get("worker_count", 1)
    print(f"\n--- Timing breakdown (overlapping; not additive): {label} ---")
    items = [
        ("Inventory cache load", stats.get("t_cache_load", 0), True),
        ("Discovery (wall)", stats.get("t_discovery_wall", 0), True),
        ("  scandir walk", stats.get("t_discovery_walk", 0), True),
        ("  queue put-wait", stats.get("t_discovery_queue_wait", 0), True),
        ("realpath() calls", stats.get("t_realpath", 0), True),
        ("stat() calls", stats.get("t_stat", 0), True),
        ("TSV cache lookups", stats.get("t_cache_lookup", 0), True),
        ("Dispatch wait", stats.get("t_dispatch_wait", 0), True),
        ("Results drain", stats.get("t_results_wait", 0), True),
        (f"ExifTool ({worker_count}w sum)", stats.get("t_exiftool", 0), False),
        (
            f"Content hashing ({worker_count}w sum)",
            stats.get("t_hashing", 0),
            False,
        ),
        (f"Geolocation ({worker_count}w sum)", stats.get("t_geolocate", 0), False),
        ("Checkpoints", stats.get("t_checkpoints", 0), True),
        ("Final write", stats.get("t_write", 0), True),
    ]
    for name, secs, show_pct in items:
        if show_pct:
            pct = secs / wall * 100 if wall else 0
            print(f"  {name:<26s} {secs:7.2f}s  ({pct:5.1f}% of wall)")
        else:
            print(f"  {name:<26s} {secs:7.2f}s  (sum across workers)")
    print(f"  {'Wall total':<26s} {wall:7.2f}s")
    worker_details = stats.get("worker_details") or []
    worker_active_total = sum(item.get("t_active", 0) for item in worker_details)
    if worker_active_total:
        print(
            f"  {'Worker active total':<26s} {worker_active_total:7.2f}s  (parallel; sum exceeds wall by design)"
        )
    if worker_details:
        print("  Per-worker:")
        for item in worker_details:
            print(
                "    "
                f"#{item.get('worker_id', 0):<2d} "
                f"wall={item.get('wall', 0):.2f}s "
                f"active={item.get('t_active', 0):.2f}s "
                f"exif={item.get('t_exiftool', 0):.2f}s "
                f"hash={item.get('t_hashing', 0):.2f}s "
                f"geo={item.get('t_geolocate', 0):.2f}s "
                f"push={item.get('t_queue_push', 0):.2f}s "
                f"files={item.get('files', 0):,} "
                f"errors={item.get('errors', 0)}"
            )
    total = stats.get("files_total", 0)
    cached = stats.get("files_cached", 0)
    processed = stats.get("files_processed", 0)
    print(f"  Files: {total:,} total ({cached:,} cached, {processed:,} processed)")


def add_hashes_to_inventory(
    tsv_path,
    hash_options,
    quiet=False,
    debug=False,
    path_style="linux",  # safe internal default; CLI default is "auto" (resolved before calling)
):
    """Fill in missing content hashes for an existing inventory TSV.

    Reads the TSV, computes hashes for rows with blank content_hash
    (where the file still exists and matches size/mtime), writes back.
    Uses the SQLite hash cache to avoid recomputing known hashes.
    """
    if not os.path.exists(tsv_path):
        print(f"Error: Inventory file '{tsv_path}' not found.")
        return False

    if hash_options.hash_mode == "off":
        print(
            "Error: --add-hashes requires hashing to be enabled. Use --hash sample or --hash full."
        )
        return False

    # Read existing TSV data
    # We use load_cache-style parsing but need the full rows including content_hash
    rows = []  # list of dicts with all TSV columns
    comment_lines = []  # preserve header comments
    header_line = None

    try:
        with open(tsv_path, "r", encoding="utf-8", newline="") as f:
            for line in f:
                line = line.rstrip("\n\r")
                if not line:
                    continue
                if line.startswith("#"):
                    comment_lines.append(line)
                    continue
                if header_line is None:
                    header_line = line
                    # Validate that this looks like the expected TSV header
                    header_fields = header_line.split("\t")
                    if header_fields[0] != "filepath":
                        print(
                            f"Error: Expected TSV header starting with 'filepath', got: {header_fields[0]!r}"
                        )
                        print(
                            f"  File '{tsv_path}' does not appear to be a valid findphotodates inventory."
                        )
                        return False
                    continue
                # Data row — use csv.reader to handle quoted fields containing
                # tabs (the writer uses csv.writer with QUOTE_MINIMAL).
                # Pad with empty strings if row has fewer fields than
                # TSV_COLUMNS (e.g. older inventories without content_hash).
                parts = next(csv.reader([line], delimiter="\t"))
                if len(parts) > len(TSV_COLUMNS):
                    if not quiet:
                        print(
                            f"WARNING: Row has {len(parts)} fields, expected {len(TSV_COLUMNS)}; "
                            f"extra fields will be dropped: {parts[0]!r}"
                        )
                row = dict(
                    zip(
                        TSV_COLUMNS,
                        parts + [""] * (len(TSV_COLUMNS) - len(parts)),
                        strict=False,
                    )
                )
                rows.append(row)
    except (IOError, UnicodeDecodeError) as e:
        print(f"Error reading '{tsv_path}': {e}")
        return False

    if not rows:
        if not quiet:
            print(f"No data rows found in '{tsv_path}'.")
        return True

    # Count how many need hashes
    need_hash = [r for r in rows if not r.get("content_hash", "").strip()]
    if not need_hash:
        if not quiet:
            print(f"All {len(rows)} rows already have content hashes.")
        return True

    if not quiet:
        print(
            f"Loaded {len(rows)} rows from '{tsv_path}'. {len(need_hash)} missing hashes."
        )
        msg = f"Hashing mode: {hash_options.hash_mode}"
        if hash_options.hash_mode == "sample":
            msg += f" ({hash_options.sample_chunks}x{hash_options.sample_chunk_mib:g} MiB {hash_options.sample_algo})"
        print(msg)

    # Open hash cache
    hash_conn = (
        open_sqlite_cache(hash_options.hash_cache_path, debug=debug)
        if (hash_options.use_hash_cache and hash_options.hash_mode != "off")
        else None
    )
    hash_cache_pending = [0] if hash_conn else None

    filled = 0
    skipped = 0
    errors = 0
    interrupted = False
    start_time = time.time()
    last_progress_time = start_time

    try:
        for row in rows:
            if row.get("content_hash", "").strip():
                continue  # already has hash

            filepath = row.get("filepath", "")
            if not filepath:
                continue

            try:
                # key_path from stored filepath (matches load_cache keying)
                key_path = _normalize_cache_key(filepath)
                # Resolve cross-platform display paths back to a local path
                # before following symlinks for stat/hash I/O.
                real_path = os.path.realpath(_resolve_local_inventory_path(filepath))
                stat_info = os.stat(real_path)
                size_bytes = stat_info.st_size
                mtime_ns = stat_info.st_mtime_ns

                # Verify file hasn't changed since indexing
                row_size = int(row.get("size_bytes", 0))
                row_mtime = int(row.get("mtime_ns", 0))
                if row_size != size_bytes or row_mtime != mtime_ns:
                    if debug:
                        print(f"  Skipping (changed): {filepath}")
                    skipped += 1
                    continue

                content_hash = get_content_hash(
                    key_path,
                    size_bytes,
                    mtime_ns,
                    hash_options,
                    hash_conn,
                    hash_cache_pending,
                    os_path=real_path,
                )
                if content_hash:
                    row["content_hash"] = content_hash
                    filled += 1
                else:
                    errors += 1

            except OSError:
                skipped += 1
                continue

            # Progress every 2 seconds
            if not quiet:
                current_time = time.time()
                if current_time - last_progress_time >= 2.0:
                    elapsed = current_time - start_time
                    print(
                        f"  Hashed {filled:,} / {len(need_hash):,} — {skipped} skipped, {errors} errors [{_format_duration(elapsed)}]",
                        end="\r" if _IS_TTY else "\n",
                    )
                    last_progress_time = current_time

    except KeyboardInterrupt:
        interrupted = True
        print(f"\n\nInterrupted! {filled:,} hashes computed so far.")
        if filled > 0:
            print("Saving partial progress...")
        else:
            print("No hashes were added. File unchanged.")

    # Commit any pending hash cache writes
    if hash_conn:
        try:
            if hash_cache_pending and hash_cache_pending[0] > 0:
                hash_conn.commit()
            hash_conn.close()
        except Exception:
            pass

    if not interrupted and not quiet:
        elapsed = time.time() - start_time
        print(
            f"\n  Done: {filled:,} hashes added, {skipped} skipped, {errors} errors [{_format_duration(elapsed)}]"
        )

    if filled == 0:
        if not interrupted and not quiet:
            print("No hashes were added. File unchanged.")
        return not interrupted

    # Reconstruct the hash_options comment lines to reflect current settings
    # Update or add hash_mode/content_hash_format headers
    new_comments = []
    has_hash_mode = False
    has_hash_format = False
    for line in comment_lines:
        if line.startswith("# inventory_root="):
            root = line.split("=", 1)[1]
            new_comments.append(
                f"# inventory_root={format_path_style(root, path_style)}"
            )
        elif line.startswith("# hash_mode="):
            new_comments.append(f"# hash_mode={hash_options.hash_mode}")
            has_hash_mode = True
        elif line.startswith("# content_hash_format="):
            if hash_options.hash_mode == "sample":
                new_comments.append("# content_hash_format=samplehash_v1")
            elif hash_options.hash_mode == "full":
                new_comments.append("# content_hash_format=fullhash_v1")
            has_hash_format = True
        elif line.startswith("# samplehash_v1 ") and hash_options.hash_mode == "sample":
            new_comments.append(
                f"# samplehash_v1 algo={hash_options.sample_algo} chunks={hash_options.sample_chunks} chunk_mib={hash_options.sample_chunk_mib:g}"
            )
        elif line.startswith("# fullhash ") and hash_options.hash_mode == "full":
            new_comments.append(f"# fullhash algo={hash_options.full_algo}")
        else:
            new_comments.append(line)

    # Add headers if they didn't exist
    insert_pos = len(new_comments)
    for i, line in enumerate(new_comments):
        if line == TSV_HEADER:
            insert_pos = i
            break
    if not has_hash_mode:
        new_comments.insert(insert_pos, f"# hash_mode={hash_options.hash_mode}")
        insert_pos += 1
    if not has_hash_format:
        if hash_options.hash_mode == "sample":
            new_comments.insert(insert_pos, "# content_hash_format=samplehash_v1")
            insert_pos += 1
            new_comments.insert(
                insert_pos,
                f"# samplehash_v1 algo={hash_options.sample_algo} chunks={hash_options.sample_chunks} chunk_mib={hash_options.sample_chunk_mib:g}",
            )
        elif hash_options.hash_mode == "full":
            new_comments.insert(insert_pos, "# content_hash_format=fullhash_v1")
            insert_pos += 1
            new_comments.insert(insert_pos, f"# fullhash algo={hash_options.full_algo}")

    # Write back atomically
    output_dir = os.path.dirname(os.path.abspath(tsv_path))
    fd, temp_file = tempfile.mkstemp(
        dir=output_dir, suffix=".tsv", prefix=".findphotodates_"
    )
    try:
        with os.fdopen(fd, "w", encoding="utf-8", newline="") as f:
            for line in new_comments:
                f.write(line + "\n")
            writer = csv.writer(f, delimiter="\t", quoting=csv.QUOTE_MINIMAL)
            writer.writerow(TSV_COLUMNS)
            sorted_rows = sorted(rows, key=lambda r: r.get("filepath", ""))
            for row in sorted_rows:
                writer.writerow(
                    [
                        format_path_style(row.get("filepath", ""), path_style),
                        row.get("date_taken", ""),
                        row.get("size_bytes", ""),
                        row.get("mtime_ns", ""),
                        row.get("gps_lat", ""),
                        row.get("gps_lon", ""),
                        row.get("location", ""),
                        row.get("content_hash", ""),
                    ]
                )
        os.replace(temp_file, tsv_path)
    except Exception:
        try:
            os.unlink(temp_file)
        except Exception:
            pass
        raise

    if interrupted:
        remaining = len(need_hash) - filled - skipped - errors
        print(
            f"Saved {filled:,} hashes to '{tsv_path}'. {remaining:,} rows still need hashes."
        )
        print("\nTo resume, run the same command again:")
        print(f'  findphotodates.py -o "{tsv_path}" --add-hashes')
        print("Already-hashed rows will be skipped automatically.")
        return False

    if not quiet:
        print(f"Updated '{tsv_path}' with {filled:,} new hashes.")
    return True


