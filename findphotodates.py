_DISCOVERY_QUEUE_SIZE = 50_000  # bounded queue for large-tree scans


class _DiscoveryState:
    """Shared state between discovery producer thread and consumer."""

    __slots__ = (
        "discovered",
        "done",
        "done_time",
        "error",
        "t_queue_wait",
        "t_walk",
        "total",
    )

    def __init__(self):
        self.discovered = (
            0  # files found so far (written by producer, read by consumer)
        )
        self.total = 0  # final count (valid only after done is set)
        self.done = threading.Event()
        self.error = None  # exception from producer, if any
        self.done_time = 0.0  # wall-clock time when discovery finished
        self.t_walk = 0.0  # time spent inside scandir walk (excl. queue blocking)
        self.t_queue_wait = 0.0  # time spent blocked on queue.put()


def _discovery_producer(file_queue, directory, extensions, state, debug=False):
    """Producer thread: walk directory, enqueue file tuples, signal done.

    Each queued item is (filepath, is_symlink, size_bytes, mtime_ns).
    For non-symlinks, size/mtime come from the scandir stat cache (free on Linux).
    For symlinks, size/mtime are None — the consumer stats the resolved target.

    Measures two non-overlapping time buckets inside the loop:
      t_walk  — time spent advancing the find_files generator (scandir I/O)
      t_queue — time spent blocked on queue.put() (backpressure from consumer)
    Their sum ≈ producer wall time (minus small loop overhead).
    """
    try:
        count = 0
        t_walk = 0.0
        t_queue = 0.0
        it = iter(find_files(directory, extensions, debug=debug))
        while True:
            tw0 = time.monotonic()
            try:
                item = next(it)  # (filepath, is_symlink, size_bytes, mtime_ns)
            except StopIteration:
                t_walk += time.monotonic() - tw0
                break
            t_walk += time.monotonic() - tw0

            tq0 = time.monotonic()
            file_queue.put(item)  # blocks if queue full
            t_queue += time.monotonic() - tq0

            count += 1
            state.discovered = count  # atomic on CPython (int store)
        state.total = count
        state.t_walk = t_walk
        state.t_queue_wait = t_queue
    except Exception as e:
        state.error = e
    finally:
        state.done_time = time.time()
        state.done.set()
        # Push sentinel so consumer doesn't block forever on get()
        try:
            file_queue.put(None, timeout=5)
        except Exception:
            pass


