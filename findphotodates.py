def compute_samplehash_v1(path, chunk_bytes, num_chunks, algo="blake3"):
    """Compute a fast sample fingerprint from path (size + \0 + chunks).

    For small files (size <= num_chunks * chunk_bytes), hashes the whole file.
    Supports blake3, blake2b(digest_size=16), or sha256.
    Returns hex digest string or None on error.
    """
    try:
        st = os.stat(path)
        size = st.st_size
    except OSError:
        return None

    if algo == "blake3" and _BLAKE3_AVAILABLE:
        h = blake3.blake3()
    elif algo == "sha256":
        h = hashlib.sha256()
    else:
        h = hashlib.blake2b(digest_size=16)

    h.update(str(size).encode("ascii"))
    h.update(b"\0")

    total_sample_bytes = num_chunks * chunk_bytes
    small_file = size <= total_sample_bytes or size == 0

    try:
        with open(path, "rb") as f:
            if small_file:
                # Stream the entire file in chunks to avoid memory blow-up
                for data in iter(lambda: f.read(65536), b""):
                    h.update(data)
            else:
                # Distribute offsets: first 0, last size-chunk_bytes, middle ones spread evenly
                chunks_to_read = []
                end_offset = max(0, size - chunk_bytes)
                for i in range(num_chunks):
                    if i == 0:
                        offset = 0
                    elif i == num_chunks - 1:
                        offset = end_offset
                    else:
                        offset = (
                            (i * end_offset) // (num_chunks - 1)
                            if num_chunks > 1
                            else 0
                        )
                    offset = max(0, min(offset, end_offset))
                    chunks_to_read.append(offset)

                # Deduplicate and sort offsets
                for offset in sorted(set(chunks_to_read)):
                    f.seek(offset)
                    data = f.read(chunk_bytes)
                    if data:
                        h.update(data)
        return h.hexdigest()
    except OSError:
        return None


def compute_fullhash(path, algo="blake3"):
    """Compute full-file hash. algo is 'blake3', 'blake2b', or 'sha256'. Returns hex digest or None."""
    try:
        if algo == "blake3" and _BLAKE3_AVAILABLE:
            h = blake3.blake3()
        elif algo == "blake2b":
            h = hashlib.blake2b()
        elif algo in ("sha256", "blake3"):
            # blake3 requested but not available — fall back to sha256
            h = hashlib.sha256()
        else:
            print(
                f"WARNING: Unknown full-hash algo {algo!r}, falling back to sha256",
                file=sys.stderr,
            )
            h = hashlib.sha256()
        with open(path, "rb") as f:
            for chunk in iter(lambda: f.read(65536), b""):
                h.update(chunk)
        return h.hexdigest()
    except OSError:
        return None


def open_sqlite_cache(cache_path, debug=False):
    """Create cache dir if needed, open SQLite connection, ensure table exists. Returns connection or None on error."""
    try:
        import sqlite3
    except ImportError:
        if debug:
            print("Debug: sqlite3 module not found.")
        return None
    try:
        Path(cache_path).parent.mkdir(parents=True, exist_ok=True)
        conn = sqlite3.connect(cache_path, check_same_thread=False)
        conn.execute("PRAGMA journal_mode=WAL;")
        conn.execute("PRAGMA synchronous=NORMAL;")
        conn.execute("""
            CREATE TABLE IF NOT EXISTS hash_cache (
                path TEXT NOT NULL,
                size_bytes INTEGER NOT NULL,
                mtime_ns INTEGER NOT NULL,
                hash_mode TEXT NOT NULL,
                chunk_bytes INTEGER NOT NULL,
                chunks INTEGER NOT NULL,
                algo TEXT NOT NULL,
                digest TEXT NOT NULL,
                PRIMARY KEY (path, size_bytes, mtime_ns, hash_mode, chunk_bytes, chunks, algo)
            )
        """)
        conn.commit()
        return conn
    except Exception as e:
        if debug:
            print(f"Debug: Failed to open hash cache at {cache_path}: {e}")
        return None


def get_cached_hash(
    conn, path, size_bytes, mtime_ns, hash_mode, chunk_bytes, chunks, algo
):
    """Return cached digest string if present, else None."""
    if conn is None:
        return None
    try:
        with _sqlite_lock:
            row = conn.execute(
                """SELECT digest FROM hash_cache
                   WHERE path = ? AND size_bytes = ? AND mtime_ns = ?
                     AND hash_mode = ? AND chunk_bytes = ? AND chunks = ? AND algo = ?""",
                (path, size_bytes, mtime_ns, hash_mode, chunk_bytes, chunks, algo),
            ).fetchone()
        return row[0] if row else None
    except Exception:
        return None


def put_cached_hash(
    conn,
    path,
    size_bytes,
    mtime_ns,
    hash_mode,
    chunk_bytes,
    chunks,
    algo,
    digest,
    pending_counter=None,
):
    """Store digest in cache. If pending_counter is a list [n], batch-commit every HASH_CACHE_BATCH_COMMIT_EVERY."""
    if conn is None:
        return
    try:
        with _sqlite_lock:
            conn.execute(
                """INSERT OR REPLACE INTO hash_cache
                   (path, size_bytes, mtime_ns, hash_mode, chunk_bytes, chunks, algo, digest)
                   VALUES (?, ?, ?, ?, ?, ?, ?, ?)""",
                (
                    path,
                    size_bytes,
                    mtime_ns,
                    hash_mode,
                    chunk_bytes,
                    chunks,
                    algo,
                    digest,
                ),
            )
            if pending_counter is not None:
                pending_counter[0] += 1
                if pending_counter[0] >= HASH_CACHE_BATCH_COMMIT_EVERY:
                    conn.commit()
                    pending_counter[0] = 0
            else:
                conn.commit()
    except Exception:
        pass


