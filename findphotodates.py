# Define supported photo file extensions
PHOTO_EXTENSIONS = {"jpg", "jpeg", "nef", "orf"}
# Define common video file extensions
VIDEO_EXTENSIONS = {"mp4", "mov", "avi", "mkv", "wmv", "flv", "webm", "m4v", "3gp"}
# Default extensions (both photos and videos)
DEFAULT_EXTENSIONS = PHOTO_EXTENSIONS.union(VIDEO_EXTENSIONS)

# TSV header identifier
TSV_HEADER = "# Generated by findphotodates.py"
TSV_COLUMNS = ["filepath", "date_taken", "size_bytes", "mtime_ns", "gps_lat", "gps_lon", "location"]

def is_supported_file(filename, extensions):
    """Check if a file has a supported extension."""
    return any(filename.lower().endswith(f".{ext}") for ext in extensions)

def find_files(directory, extensions):
    """Recursively find all files with the specified extensions in the given directory."""
    for root, _, files in os.walk(directory):
        for file in files:
            if is_supported_file(file, extensions):
                yield os.path.join(root, file)

def get_photo_data(filepath):
    """Use exiftool to get the date and GPS data of a photo/video.
    
    Always extracts GPS data so it can be cached and used later
    without re-reading EXIF.
    """
    try:
        # Call exiftool to extract the Date/Time Original tag, fall back to other date fields
        # Always extract GPS data so we can cache it for later --locate runs
        date_tags = ["-DateTimeOriginal", "-CreateDate", "-MediaCreateDate"]
        tags = date_tags.copy()
        tags.extend(["-GPSLatitude", "-GPSLongitude"])

        result = subprocess.run(
            ["exiftool", "-n", "-f", *tags, "-s", "-s", "-s", filepath],
            stdout=subprocess.PIPE,
            stderr=subprocess.PIPE,
            text=True
        )
        
        if result.returncode == 0:
            lines = result.stdout.splitlines()
            # With -f, we typically get 5 lines: 3 date tags + 2 GPS tags
            # date fields are lines[0:3]; GPS are lines[3], lines[4]
            # But exiftool can emit extra lines in edge cases, so be defensive
            
            # Process date - try each possible date field
            date = None
            if len(lines) >= len(date_tags):
                for i in range(len(date_tags)):
                    line = lines[i].strip()
                    if line and line != "-":
                        # Basic validation that this looks like a date
                        if re.match(r'\d{4}:\d{2}:\d{2} \d{2}:\d{2}:\d{2}', line):
                            date = line
                            break
            
            # Always extract GPS data (for caching) - parse by position
            gps_lat = None
            gps_lon = None
            if len(lines) >= len(date_tags) + 2:
                gps_lat_str = lines[len(date_tags)].strip()
                gps_lon_str = lines[len(date_tags) + 1].strip()
                if gps_lat_str and gps_lat_str != "-":
                    gps_lat = gps_lat_str
                if gps_lon_str and gps_lon_str != "-":
                    gps_lon = gps_lon_str
                            
            return date, gps_lat, gps_lon
        else:
            return None, None, None
    except FileNotFoundError as e:
        raise FileNotFoundError("Exiftool is required but was not found. Please install it.") from e

# Global variables for geolocation rate limiting and coordinate caching
_last_geolocate_time = 0
_coord_cache = {}  # (round(lat,5), round(lon,5)) -> location

def geolocate(lat, lon):
    """Convert GPS coordinates to a human-readable location using Nominatim API.
    
    Implements rate limiting (1 second between requests) and in-memory coordinate caching
    to avoid duplicate API calls for the same location within a single run.
    Falls back to coordinates if the request fails.
    
    Note: The coordinate cache is in-memory only and does not persist between runs.
    However, location data in the TSV output file serves as a persistent cache - once
    a location is computed and saved, it will be reused from the TSV cache on subsequent runs.
    """
    global _last_geolocate_time, _coord_cache
    
    if not lat or not lon:
        return None
    
    try:
        # Convert to float to ensure valid coordinates
        lat_float = float(lat)
        lon_float = float(lon)
    except (ValueError, TypeError):
        return f"{lat}, {lon}"
    
    # Check coordinate cache (round to 5 decimal places ~1.1m precision)
    cache_key = (round(lat_float, 5), round(lon_float, 5))
    if cache_key in _coord_cache:
        return _coord_cache[cache_key]
    
    # Rate limiting: wait at least 1 second since last request
    current_time = time.time()
    time_since_last = current_time - _last_geolocate_time
    if time_since_last < 1.0:
        time.sleep(1.0 - time_since_last)
    
    try:
        # Build Nominatim reverse geocoding URL with addressdetails=1
        url = f"https://nominatim.openstreetmap.org/reverse?format=json&lat={lat_float}&lon={lon_float}&addressdetails=1"
        
        # Create request with proper User-Agent header (required by Nominatim)
        req = urllib.request.Request(url)
        req.add_header('User-Agent', 'findphotodates.py/1.1 (https://github.com/alanrockefeller/findphotodates.py)')
        
        # Make the request
        with urllib.request.urlopen(req, timeout=10) as response:
            data = json.loads(response.read().decode())
            
            # Update last request time
            _last_geolocate_time = time.time()
            
            # Extract location from response - prefer address components over display_name
            location = None
            if 'address' in data:
                addr = data['address']
                parts = []
                # Build location from address components in order of specificity
                for key in ['city', 'town', 'village', 'county', 'state', 'country']:
                    if key in addr:
                        parts.append(addr[key])
                if parts:
                    location = ', '.join(parts)
            
            # Fallback to display_name if address components didn't work
            if not location and 'display_name' in data:
                location = data['display_name']
            
            # Fallback to coordinates if no location found
            if not location:
                location = f"{lat_float}, {lon_float}"
            
            # Cache the result
            _coord_cache[cache_key] = location
            return location
            
    except urllib.error.HTTPError as e:
        # Handle 429 (rate limit) with longer backoff
        if e.code == 429:
            _last_geolocate_time = time.time() + 5  # Wait 5 seconds before next attempt
        else:
            _last_geolocate_time = time.time()
        # Don't cache fallback coordinates - allow retry on next run
        return f"{lat_float}, {lon_float}"
    except Exception:
        # Don't cache fallback coordinates - allow retry on next run
        _last_geolocate_time = time.time()
        return f"{lat_float}, {lon_float}"

def is_coordinate_string(location):
    """Check if location string looks like raw coordinates (e.g., "37.7, -122.4")."""
    if not location:
        return False
    # Match pattern: optional negative, digits, optional decimal, comma, optional space, optional negative, digits, optional decimal
    return bool(re.match(r'^-?\d+(\.\d+)?,\s*-?\d+(\.\d+)?$', location.strip()))

def load_cache(output_file):
    """Load existing TSV cache if it exists and has the expected header."""
    cache = {}
    if not os.path.exists(output_file):
        return cache
    
    try:
        with open(output_file, 'r', encoding='utf-8') as f:
            first_line = f.readline().strip()
            if first_line != TSV_HEADER:
                return cache
            
            # Let DictReader read the header automatically
            reader = csv.DictReader(f, delimiter='\t')
            
            # Verify header matches expected columns
            if reader.fieldnames != TSV_COLUMNS:
                return cache
            
            for row in reader:
                filepath = row.get('filepath', '')
                if filepath:
                    try:
                        # Normalize to real path (follows symlinks) for cache key consistency
                        abs_filepath = os.path.realpath(filepath)
                        size_bytes = int(row.get('size_bytes', '0'))
                        mtime_ns = int(row.get('mtime_ns', '0'))
                        date_taken = row.get('date_taken', '') or None
                        gps_lat = row.get('gps_lat', '') or None
                        gps_lon = row.get('gps_lon', '') or None
                        location = row.get('location', '') or None
                        
                        # Treat coordinate strings as missing location (allows retry)
                        if location and is_coordinate_string(location):
                            location = None
                        
                        cache[abs_filepath] = {
                            'size_bytes': size_bytes,
                            'mtime_ns': mtime_ns,
                            'date_taken': date_taken,
                            'gps_lat': gps_lat,
                            'gps_lon': gps_lon,
                            'location': location
                        }
                    except (ValueError, KeyError):
                        continue
    except (IOError, UnicodeDecodeError):
        return cache
    
    return cache

def write_dates_to_file_atomic(output_file, photo_data):
    """Write photo data to TSV file atomically (temp file then replace) using csv.writer."""
    # Create a temporary file in the same directory as the output file
    output_dir = os.path.dirname(os.path.abspath(output_file))
    if not output_dir:
        output_dir = '.'
    
    fd, temp_file = tempfile.mkstemp(dir=output_dir, suffix='.tsv', prefix='.findphotodates_')
    
    try:
        with os.fdopen(fd, 'w', encoding='utf-8', newline='') as f:
            # Write header identifier
            f.write(TSV_HEADER + '\n')
            
            # Use csv.writer for proper TSV escaping
            writer = csv.writer(f, delimiter='\t', quoting=csv.QUOTE_MINIMAL)
            
            # Write column headers
            writer.writerow(TSV_COLUMNS)
            
            # Sort by filepath for consistent output ordering
            sorted_data = sorted(photo_data, key=lambda r: r[0])
            
            # Write data rows
            for filepath, date_taken, size_bytes, mtime_ns, gps_lat, gps_lon, location in sorted_data:
                writer.writerow([
                    filepath,
                    date_taken or '',
                    str(size_bytes),
                    str(mtime_ns),
                    gps_lat or '',
                    gps_lon or '',
                    location or ''
                ])
        
        # Atomically replace the output file using os.replace()
        os.replace(temp_file, output_file)
    except Exception:
        # Clean up temp file on error
        try:
            os.unlink(temp_file)
        except Exception:
            pass
        raise

def summarize_results(photo_data, file_counts, quiet, debug=False):
    """Print a summary of results."""
    if quiet:
        return

    print("\nSummary of Results:\n")

    # Print counts by file type
    print("Files found:")
    for ext, count in sorted(file_counts.items()):
        print(f"  {ext.upper()}: {count}")

    # Calculate date summaries
    date_counter = Counter()
    for entry in photo_data:
        # Entry is a tuple: (filepath, date_taken, size_bytes, mtime_ns, gps_lat, gps_lon, location)
        date_str = entry[1] if len(entry) > 1 else None
        
        # Skip None or empty dates
        if not date_str:
            continue
        
        try:
            # Parse exiftool date format: YYYY:MM:DD HH:MM:SS
            dt = datetime.strptime(date_str, "%Y:%m:%d %H:%M:%S")
            day_key = dt.strftime("%Y-%m-%d")
            date_counter[day_key] += 1
        except (ValueError, AttributeError, TypeError) as e:
            # Gracefully skip invalid dates
            if debug:
                print(f"Warning: Could not parse date '{date_str}': {str(e)}")
            continue

    # Determine file type label for summary
    file_type_label = "Media" 
    if all(ext in VIDEO_EXTENSIONS for ext in file_counts):
        file_type_label = "Videos"
    elif all(ext in PHOTO_EXTENSIONS for ext in file_counts):
        file_type_label = "Photos"
            
    if len(date_counter) <= 20:
        print(f"\n{file_type_label} Taken per day:")
        for day, count in sorted(date_counter.items()):
            print(f"  {day}: {count}")
    else:
        month_counter = defaultdict(int)
        for day_key in date_counter:
            try:
                # Parse the day_key (YYYY-MM-DD) to get year-month
                dt = datetime.strptime(day_key, "%Y-%m-%d")
                year_month_key = dt.strftime("%Y-%m")
                month_counter[year_month_key] += date_counter[day_key]
            except (ValueError, AttributeError) as e:
                # Gracefully skip invalid day keys
                if debug:
                    print(f"Warning: Could not extract year-month from '{day_key}': {str(e)}")
                continue
        print(f"\n{file_type_label} Taken per Month:")
        for year_month, count in sorted(month_counter.items()):
            print(f"  {year_month}: {count}")

