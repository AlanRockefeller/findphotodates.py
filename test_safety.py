class TestIterInventoryRows(unittest.TestCase):
    """Tests for the inventory parser."""

    def setUp(self):
        self.tmp = tempfile.mkdtemp()

    def tearDown(self):
        shutil.rmtree(self.tmp, ignore_errors=True)

    def test_valid_tsv(self):
        """Standard 8-column TSV with header — should parse correctly."""
        lines = _make_tsv_rows([
            ["/photos/a.jpg", "2024:01:01 12:00:00", "1234", "1700000000000000000",
             "37.7", "-122.4", "", "abcdef01"],
        ])
        inv = _write_tsv(self.tmp, lines)
        results = list(iter_inventory_rows(inv))
        self.assertEqual(len(results), 1)
        filepath, date, size, mtime, chash, hdrs = results[0]
        self.assertEqual(filepath, "/photos/a.jpg")
        self.assertEqual(date, "2024:01:01 12:00:00")
        self.assertEqual(size, 1234)
        self.assertEqual(mtime, 1700000000000000000)
        self.assertEqual(chash, "abcdef01")

    def test_valid_tsv_blank_hash(self):
        """Rows with blank content_hash should yield empty string, not None."""
        lines = _make_tsv_rows([
            ["/photos/b.jpg", "2024:02:01 08:00:00", "5678", "1700000000000000001",
             "", "", "", ""],
        ])
        inv = _write_tsv(self.tmp, lines)
        results = list(iter_inventory_rows(inv))
        self.assertEqual(len(results), 1)
        self.assertEqual(results[0][4], "")

    def test_rejects_tsv_without_header(self):
        """TSV data without a 'filepath' header row must be rejected entirely."""
        lines = [
            "# inventory_root=/photos",
            "# Generated by findphotodates.py",
            "/photos/a.jpg\t2024:01:01\t1234\t170000\t\t\t\tabcdef",
        ]
        inv = _write_tsv(self.tmp, lines)
        results = list(iter_inventory_rows(inv))
        self.assertEqual(results, [], "Should yield nothing when TSV header is missing")

    def test_non_hex_hash_treated_as_empty(self):
        """Non-hex content_hash values must be treated as empty (not matched)."""
        lines = _make_tsv_rows([
            ["/photos/c.jpg", "2024:01:01 12:00:00", "1000", "1700000000000000000",
             "", "", "", "NOT_A_HEX_HASH"],
        ])
        inv = _write_tsv(self.tmp, lines)
        results = list(iter_inventory_rows(inv))
        self.assertEqual(len(results), 1)
        self.assertEqual(results[0][4], "", "Non-hex hash should be treated as empty")

    def test_negative_size_treated_as_none(self):
        """Negative size_bytes should be treated as None (unknown)."""
        lines = _make_tsv_rows([
            ["/photos/d.jpg", "2024:01:01 12:00:00", "-5", "1700000000000000000",
             "", "", "", "aabb"],
        ])
        inv = _write_tsv(self.tmp, lines)
        results = list(iter_inventory_rows(inv, debug=True))
        self.assertEqual(len(results), 1)
        self.assertIsNone(results[0][2], "Negative size should become None")

    def test_non_numeric_size_treated_as_none(self):
        """Non-numeric size_bytes should become None."""
        lines = _make_tsv_rows([
            ["/photos/e.jpg", "2024:01:01 12:00:00", "notanumber", "1700000000000000000",
             "", "", "", ""],
        ])
        inv = _write_tsv(self.tmp, lines)
        results = list(iter_inventory_rows(inv))
        self.assertEqual(len(results), 1)
        self.assertIsNone(results[0][2])

    def test_old_format(self):
        """OLD format lines (./path: YYYY:MM:DD HH:MM:SS) should parse."""
        lines = [
            "./photos/old.jpg: 2020:06:15 09:30:00",
            "./photos/old2.jpg: 2021:12:25 18:00:00",
        ]
        inv = _write_tsv(self.tmp, lines)
        results = list(iter_inventory_rows(inv))
        self.assertEqual(len(results), 2)
        self.assertEqual(results[0][0], "./photos/old.jpg")
        self.assertEqual(results[0][1], "2020:06:15 09:30:00")
        self.assertIsNone(results[0][2])
        self.assertEqual(results[0][4], "")

    def test_headers_parsed(self):
        """Comment headers (# key=value) should be available in yielded tuples."""
        lines = _make_tsv_rows(
            [["/a.jpg", "", "100", "1000", "", "", "", ""]],
            headers={"inventory_root": "/mnt/drive1", "hash_mode": "sample"},
        )
        inv = _write_tsv(self.tmp, lines)
        results = list(iter_inventory_rows(inv))
        self.assertEqual(len(results), 1)
        hdrs = results[0][5]
        self.assertEqual(hdrs["inventory_root"], "/mnt/drive1")
        self.assertEqual(hdrs["hash_mode"], "sample")

    def test_samplehash_config_header(self):
        """samplehash_v1 config line should be parsed into headers."""
        lines = [
            "# content_hash_format=samplehash_v1",
            "# samplehash_v1 algo=blake3 chunks=3 chunk_mib=0.0625",
            "# Generated by findphotodates.py",
            "filepath\tdate_taken\tsize_bytes\tmtime_ns\tgps_lat\tgps_lon\tlocation\tcontent_hash",
            "/a.jpg\t\t100\t1000\t\t\t\taabb",
        ]
        inv = _write_tsv(self.tmp, lines)
        results = list(iter_inventory_rows(inv))
        hdrs = results[0][5]
        self.assertIn("samplehash_v1", hdrs)
        self.assertIn("algo=blake3", hdrs["samplehash_v1"])

    def test_empty_filepath_rows_skipped(self):
        """Rows with empty filepath should be silently skipped."""
        lines = _make_tsv_rows([
            ["", "2024:01:01 12:00:00", "100", "1000", "", "", "", "aabb"],
            ["/valid.jpg", "2024:01:01 12:00:00", "200", "2000", "", "", "", "ccdd"],
        ])
        inv = _write_tsv(self.tmp, lines)
        results = list(iter_inventory_rows(inv))
        self.assertEqual(len(results), 1)
        self.assertEqual(results[0][0], "/valid.jpg")

    def test_zero_size_is_valid(self):
        """Size 0 is a valid file size (e.g., empty file)."""
        lines = _make_tsv_rows([
            ["/empty.jpg", "", "0", "1000", "", "", "", ""],
        ])
        inv = _write_tsv(self.tmp, lines)
        results = list(iter_inventory_rows(inv))
        self.assertEqual(results[0][2], 0)

    def test_uppercase_hex_hash_accepted(self):
        """Uppercase hex hashes should be accepted."""
        lines = _make_tsv_rows([
            ["/a.jpg", "", "100", "1000", "", "", "", "AABBCCDD"],
        ])
        inv = _write_tsv(self.tmp, lines)
        results = list(iter_inventory_rows(inv))
        self.assertEqual(results[0][4], "AABBCCDD")

    def test_hash_with_spaces_treated_as_empty(self):
        """Hash containing spaces (likely corrupt) should be treated as empty."""
        lines = _make_tsv_rows([
            ["/a.jpg", "", "100", "1000", "", "", "", "aabb ccdd"],
        ])
        inv = _write_tsv(self.tmp, lines)
        results = list(iter_inventory_rows(inv))
        self.assertEqual(results[0][4], "", "Hash with spaces should be treated as empty")


