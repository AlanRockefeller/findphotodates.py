class TestAddHashesHeaderValidation(unittest.TestCase):
    """Test that add_hashes_to_inventory rejects files without proper header."""

    def setUp(self):
        self.tmp = tempfile.mkdtemp()

    def tearDown(self):
        shutil.rmtree(self.tmp, ignore_errors=True)

    def test_rejects_non_tsv_header(self):
        """A file whose first non-comment line doesn't start with 'filepath' should be rejected."""
        from findphotodates import add_hashes_to_inventory, parse_hash_args

        inv = Path(self.tmp) / "bad.tsv"
        inv.write_text(
            "# Generated by findphotodates.py\n"
            "not_filepath\tdate\tsize\n"
            "/a.jpg\t2024:01:01\t100\n",
            encoding="utf-8",
        )

        hash_opts = parse_hash_args(hash_mode="sample")
        result = add_hashes_to_inventory(str(inv), hash_opts, quiet=True)
        self.assertFalse(result, "Should reject TSV with wrong header")

    def test_accepts_valid_header(self):
        """A file with a proper 'filepath' header and no data rows should return True."""
        from findphotodates import add_hashes_to_inventory, parse_hash_args

        inv = Path(self.tmp) / "good.tsv"
        inv.write_text(
            "# Generated by findphotodates.py\n"
            "filepath\tdate_taken\tsize_bytes\tmtime_ns\tgps_lat\tgps_lon\tlocation\tcontent_hash\n",
            encoding="utf-8",
        )

        hash_opts = parse_hash_args(hash_mode="sample")
        result = add_hashes_to_inventory(str(inv), hash_opts, quiet=True)
        self.assertTrue(result, "Should accept TSV with valid header and no data")

    def test_rejects_empty_file(self):
        """An empty file has no data rows — should return True (nothing to do)."""
        from findphotodates import add_hashes_to_inventory, parse_hash_args

        inv = Path(self.tmp) / "empty.tsv"
        inv.write_text("", encoding="utf-8")

        hash_opts = parse_hash_args(hash_mode="sample")
        result = add_hashes_to_inventory(str(inv), hash_opts, quiet=True)
        self.assertTrue(result)


