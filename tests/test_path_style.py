# Checkpoint / interrupt consistency (unit-level: verifying path_style flows)
# ---------------------------------------------------------------------------


class TestCheckpointConsistency:
    def test_checkpoint_uses_resolved_style(self, tmp_path):
        """Verify that write_dates_to_file_atomic produces consistent output
        when called with the same resolved style (simulating checkpoint vs final)."""
        out_ckpt = str(tmp_path / "ckpt.tsv")
        out_final = str(tmp_path / "final.tsv")
        data = [("/mnt/c/Photos/a.jpg", "", 100, 0, "", "", "", "")]
        kwargs = dict(
            inventory_root="/mnt/c/Photos",
            hash_options=DummyHashOpts(),
            path_style="windows",
        )
        fpd.write_dates_to_file_atomic(out_ckpt, data, **kwargs)
        fpd.write_dates_to_file_atomic(out_final, data, **kwargs)

        ckpt_comments, _, ckpt_rows = _read_tsv(out_ckpt)
        final_comments, _, final_rows = _read_tsv(out_final)

        # inventory_root must match
        ckpt_root = [c for c in ckpt_comments if c.startswith("# inventory_root=")]
        final_root = [c for c in final_comments if c.startswith("# inventory_root=")]
        assert ckpt_root == final_root

        # filepath rows must match
        assert ckpt_rows == final_rows

    def test_interrupt_save_same_as_final(self, tmp_path):
        """Same as checkpoint test but named to cover the Ctrl-C save path."""
        out1 = str(tmp_path / "interrupt.tsv")
        out2 = str(tmp_path / "final.tsv")
        data = [("/mnt/c/Data/file.txt", "2024:06:15", 2048, 99999, "", "", "", "")]
        kwargs = dict(
            inventory_root="/mnt/c/Data",
            hash_options=DummyHashOpts(),
            path_style="linux",
        )
        fpd.write_dates_to_file_atomic(out1, data, **kwargs)
        fpd.write_dates_to_file_atomic(out2, data, **kwargs)
        with open(out1, encoding="utf-8") as f1, open(out2, encoding="utf-8") as f2:
            assert f1.read() == f2.read()


