        tsv = _make_tsv(["\t".join(fpd.TSV_COLUMNS)], path=str(tmp_path))
        result = fpd.resolve_output_path_style("auto", tsv, "/home/user/photos")
        expected = "windows" if platform.system() == "Windows" else "linux"
        assert result == expected
