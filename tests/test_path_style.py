class TestAddHashesPreservesStyle:
    def test_windows_style_preserved_with_auto(self, tmp_path):
        tsv = str(tmp_path / "inv.tsv")
        lines = [
            "# inventory_root=C:\\Photos",
            "# Generated by findphotodates.py",
            "\t".join(fpd.TSV_COLUMNS),
            "C:\\Photos\\a.jpg\t2023:01:01\t100\t0\t\t\t\t",
        ]
        with open(tsv, "w", encoding="utf-8") as f:
            for line in lines:
                f.write(line + "\n")
        # Resolve auto -> should detect windows
        resolved = fpd.resolve_output_path_style("auto", tsv, None)
        assert resolved == "windows"

    def test_linux_style_preserved_with_auto(self, tmp_path):
        tsv = str(tmp_path / "inv.tsv")
        lines = [
            "# inventory_root=/mnt/c/Photos",
            "# Generated by findphotodates.py",
            "\t".join(fpd.TSV_COLUMNS),
            "/mnt/c/Photos/a.jpg\t2023:01:01\t100\t0\t\t\t\t",
        ]
        with open(tsv, "w", encoding="utf-8") as f:
            for line in lines:
                f.write(line + "\n")
        resolved = fpd.resolve_output_path_style("auto", tsv, None)
        assert resolved == "linux"

    def test_add_hashes_resolves_opposite_style_paths_locally(self, tmp_path):
        photo = tmp_path / "photo.jpg"
        photo.write_bytes(b"findphotodates path-style regression test")
        stat_info = photo.stat()

        native_path = str(photo)
        native_root = str(tmp_path)
        opposite_style = (
            "linux" if platform.system() == "Windows" else "windows"
        )
        stored_path = fpd.format_path_style(native_path, opposite_style)
        stored_root = fpd.format_path_style(native_root, opposite_style)

        tsv = str(tmp_path / "inventory.tsv")
        lines = [
            f"# inventory_root={stored_root}",
            "# Generated by findphotodates.py",
            "\t".join(fpd.TSV_COLUMNS),
            f"{stored_path}\t\t{stat_info.st_size}\t{stat_info.st_mtime_ns}\t\t\t\t",
        ]
        with open(tsv, "w", encoding="utf-8") as f:
            for line in lines:
                f.write(line + "\n")

        hash_opts = fpd.parse_hash_args(
            hash_mode="sample",
            sample_chunks=1,
            sample_chunk_mib=0.001,
            no_hash_cache=True,
            quiet=True,
        )
        resolved = fpd.resolve_output_path_style("auto", tsv, None)
        assert resolved == opposite_style

        assert fpd.add_hashes_to_inventory(
            tsv, hash_opts, quiet=True, path_style=resolved
        )

        comments, _, rows = _read_tsv(tsv)
        root_val = [c for c in comments if c.startswith("# inventory_root=")][0].split(
            "=", 1
        )[1]
        assert root_val == stored_root
        assert rows[0][0] == stored_path
        assert rows[0][7]


