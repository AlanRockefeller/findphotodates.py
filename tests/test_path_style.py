class TestWriteInventoryRoot:
    def test_windows_style_inventory_root_not_mangled_on_linux(self, tmp_path):
        """On Linux/WSL, writing with path_style='windows' must not produce
        a bogus inventory_root like /current/dir/C:\\Photos."""
        out = str(tmp_path / "out.tsv")
        data = [("/mnt/c/Photos/a.jpg", "2023:01:01", 100, 0, "", "", "", "")]
        fpd.write_dates_to_file_atomic(
            out,
            data,
            inventory_root="/mnt/c/Photos",
            hash_options=DummyHashOpts(),
            path_style="windows",
        )
        comments, _, rows = _read_tsv(out)
        root_lines = [c for c in comments if c.startswith("# inventory_root=")]
        assert len(root_lines) == 1
        root_val = root_lines[0].split("=", 1)[1]
        # Must be a clean Windows-style path
        assert (
            root_val.startswith("C:\\")
            or root_val.startswith("c:\\")
            or "\\" in root_val
        )
        assert not root_val.startswith("/")  # no bogus POSIX prefix

    def test_linux_style_inventory_root_correct(self, tmp_path):
        out = str(tmp_path / "out.tsv")
        data = [("/mnt/c/Photos/a.jpg", "2023:01:01", 100, 0, "", "", "", "")]
        fpd.write_dates_to_file_atomic(
            out,
            data,
            inventory_root="/mnt/c/Photos",
            hash_options=DummyHashOpts(),
            path_style="linux",
        )
        comments, _, rows = _read_tsv(out)
        root_lines = [c for c in comments if c.startswith("# inventory_root=")]
        assert len(root_lines) == 1
        root_val = root_lines[0].split("=", 1)[1]
        assert root_val == "/mnt/c/Photos"

    def test_windows_inventory_root_and_rows_consistent(self, tmp_path):
        out = str(tmp_path / "out.tsv")
        data = [("/mnt/c/Photos/a.jpg", "", 100, 0, "", "", "", "")]
        fpd.write_dates_to_file_atomic(
            out,
            data,
            inventory_root="/mnt/c/Photos",
            hash_options=DummyHashOpts(),
            path_style="windows",
        )
        comments, _, rows = _read_tsv(out)
        root_val = [c for c in comments if c.startswith("# inventory_root=")][0].split(
            "=", 1
        )[1]
        filepath = rows[0][0]
        # Both must be Windows-style
        assert "\\" in root_val
        assert "\\" in filepath

    def test_windows_drive_root_inventory_root_and_rows_consistent(self, tmp_path):
        out = str(tmp_path / "out.tsv")
        data = [("/mnt/c/a.jpg", "", 100, 0, "", "", "", "")]
        fpd.write_dates_to_file_atomic(
            out,
            data,
            inventory_root="/mnt/c",
            hash_options=DummyHashOpts(),
            path_style="windows",
        )
        comments, _, rows = _read_tsv(out)
        root_val = [c for c in comments if c.startswith("# inventory_root=")][0].split(
            "=", 1
        )[1]
        filepath = rows[0][0]
        assert root_val == "C:\\"
        assert filepath == "C:\\a.jpg"


