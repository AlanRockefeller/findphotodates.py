# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _make_tsv(lines, path=None):
    """Write *lines* (list of str) to a temp file and return the path."""
    fd, p = tempfile.mkstemp(suffix=".tsv", dir=path)
    with os.fdopen(fd, "w", encoding="utf-8") as f:
        for line in lines:
            f.write(line + "\n")
    return p


def _read_tsv(path):
    """Return (comment_lines, header, data_rows) from a TSV file."""
    comments = []
    header = None
    rows = []
    with open(path, "r", encoding="utf-8") as f:
        for line in f:
            line = line.rstrip("\n\r")
            if not line:
                continue
            if line.startswith("#"):
                comments.append(line)
                continue
            if header is None:
                header = line.split("\t")
                continue
            rows.append(next(csv.reader([line], delimiter="\t")))
    return comments, header, rows


class DummyHashOpts:
    hash_mode = "off"
    hash_exts = None
    use_hash_cache = False
    hash_cache_path = None
    sample_chunk_mib = 0
    sample_chunks = 0
    sample_algo = "blake2b"
    full_algo = "sha256"


