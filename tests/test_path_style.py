class TestResolveOutputPathStyle:
    def test_explicit_linux(self):
        assert fpd.resolve_output_path_style("linux", None, None) == "linux"

    def test_explicit_windows(self):
        assert fpd.resolve_output_path_style("windows", None, None) == "windows"

    def test_auto_preserves_existing_windows(self, tmp_path):
        p = _make_tsv(
            [
                "# inventory_root=C:\\Photos",
                "\t".join(fpd.TSV_COLUMNS),
                "C:\\Photos\\a.jpg\t\t100\t0\t\t\t\t",
            ],
            path=str(tmp_path),
        )
        assert fpd.resolve_output_path_style("auto", p, "/mnt/c/Photos") == "windows"

    def test_auto_preserves_existing_linux(self, tmp_path):
        p = _make_tsv(
            [
                "# inventory_root=/mnt/c/Photos",
                "\t".join(fpd.TSV_COLUMNS),
                "/mnt/c/Photos/a.jpg\t\t100\t0\t\t\t\t",
            ],
            path=str(tmp_path),
        )
        assert fpd.resolve_output_path_style("auto", p, "C:\\Photos") == "linux"

    def test_auto_infers_windows_from_directory(self):
        assert (
            fpd.resolve_output_path_style("auto", "/nonexistent.tsv", "C:\\Photos")
            == "windows"
        )

    def test_auto_infers_linux_from_wsl_directory(self):
        assert (
            fpd.resolve_output_path_style("auto", "/nonexistent.tsv", "/mnt/c/Photos")
            == "linux"
        )

    def test_auto_falls_back_to_platform(self):
        expected = "windows" if platform.system() == "Windows" else "linux"
        assert (
            fpd.resolve_output_path_style(
                "auto", "/nonexistent.tsv", "/home/user/photos"
            )
            == expected
        )


