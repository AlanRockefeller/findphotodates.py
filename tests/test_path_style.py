class TestDetectInventoryPathStyle:
    def test_detects_windows_from_inventory_root(self, tmp_path):
        p = _make_tsv(
            [
                "# inventory_root=C:\\Photos\\Vacation",
                "# Generated by findphotodates.py",
                "\t".join(fpd.TSV_COLUMNS),
                "C:\\Photos\\Vacation\\img.jpg\t\t1024\t0\t\t\t\t",
            ],
            path=str(tmp_path),
        )
        assert fpd.detect_inventory_path_style(p) == "windows"

    def test_detects_linux_from_inventory_root(self, tmp_path):
        p = _make_tsv(
            [
                "# inventory_root=/mnt/c/Photos/Vacation",
                "# Generated by findphotodates.py",
                "\t".join(fpd.TSV_COLUMNS),
                "/mnt/c/Photos/Vacation/img.jpg\t\t1024\t0\t\t\t\t",
            ],
            path=str(tmp_path),
        )
        assert fpd.detect_inventory_path_style(p) == "linux"

    def test_detects_windows_from_first_data_row(self, tmp_path):
        p = _make_tsv(
            [
                "# Generated by findphotodates.py",
                "\t".join(fpd.TSV_COLUMNS),
                "D:\\Data\\file.txt\t\t512\t0\t\t\t\t",
            ],
            path=str(tmp_path),
        )
        assert fpd.detect_inventory_path_style(p) == "windows"

    def test_detects_linux_from_first_data_row(self, tmp_path):
        p = _make_tsv(
            [
                "# Generated by findphotodates.py",
                "\t".join(fpd.TSV_COLUMNS),
                "/home/user/file.txt\t\t512\t0\t\t\t\t",
            ],
            path=str(tmp_path),
        )
        assert fpd.detect_inventory_path_style(p) == "linux"

    def test_returns_none_for_empty_file(self, tmp_path):
        p = _make_tsv([], path=str(tmp_path))
        assert fpd.detect_inventory_path_style(p) is None

    def test_returns_none_for_nonexistent_file(self):
        assert fpd.detect_inventory_path_style("/nonexistent/path.tsv") is None


